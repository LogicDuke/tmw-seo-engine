    /** @return string[] */
    private static function model_tag_names( \WP_Post $post ): array {
        $tags = get_the_terms( $post, 'post_tag' );
        if ( ! is_array( $tags ) ) {
            return [];
        }

        $names = [];
        foreach ( $tags as $tag ) {
            if ( $tag instanceof \WP_Term ) {
                $names[] = (string) $tag->slug;
            }
        }

        return array_values( array_unique( array_filter( $names ) ) );
    }

    /**
     * @param array{primary:string,additional:string[],longtail:string[],sources:array<string,mixed>} $pack
     */
    private static function starter_model_keyword_confidence( array $pack, array $platform ): float {
        $score = 54.0;
        if ( trim( (string) ( $pack['primary'] ?? '' ) ) !== '' ) {
            $score += 8.0;
        }
        $score += min( 10.0, (float) count( $pack['additional'] ?? [] ) * 2.0 );
        $score += min( 8.0, (float) count( $pack['longtail'] ?? [] ) * 1.5 );
        $score += ! empty( $platform['count'] ) ? min( 10.0, (float) $platform['count'] * 3.0 ) : 0.0;

        return min( 82.0, max( 58.0, round( $score, 1 ) ) );
    }

    public static function run_optimize_job(array $job): void {
        $post_id = (int)($job['entity_id'] ?? 0);
        $dry = (int) Settings::get('tmwseo_dry_run_mode', 0);
        if ($post_id <= 0) {
            Logs::warn('content', 'optimize_post missing entity_id');
            return;
        }

        $post = get_post($post_id);
        if (!$post) {
            Logs::warn('content', 'Post not found', ['post_id' => $post_id]);
            return;
        }

        $payload = $job['payload'] ?? [];
        if (!is_array($payload)) {
            $payload = [];
        }

        $insert_block = (int)($payload['insert_block'] ?? 1) === 1;
        $keywords_only = (int)($payload['keywords_only'] ?? 0) === 1 || (int)($payload['refresh_keywords_only'] ?? 0) === 1;

        // Strategy precedence: explicit valid payload > model safety fallback > settings/providers.
        $strategy = self::normalize_generate_strategy((string)($payload['strategy'] ?? ''), (string) $post->post_type, (int) $dry);

        if (!$keywords_only && Settings::is_safe_mode() && $strategy !== 'template') {
            Logs::info('content', 'Safe mode enabled; skipping external AI generation', ['post_id' => $post_id, 'strategy' => $strategy]);
            delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
            update_post_meta($post_id, '_tmwseo_optimize_done', 'skipped_safe_mode');
            return;
        }

        $is_manual_model_request = self::is_manual_model_request($post, $payload);
        $is_manual_category_request = self::is_manual_category_request($post, $payload);

        // Build keyword pack for models first so manual bootstrap can satisfy
        // obvious prerequisites before the content gate blocks generation.
        $keyword_pack = [];
        if ($post->post_type === 'model') {
            $keyword_pack = AssistedDraftEnrichmentService::build_and_store_keyword_pack_for_post($post, true);
            if (class_exists(TemplateContent::class) && method_exists(TemplateContent::class, 'hydrate_model_keyword_pack')) {
                $keyword_pack = TemplateContent::hydrate_model_keyword_pack($post, $keyword_pack);
            }
            if ($is_manual_model_request) {
                $keyword_pack = self::bootstrap_manual_model_generate($post, $keyword_pack);
            }
            AssistedDraftEnrichmentService::enrich_rank_math_keywords($post, $keyword_pack);
        } elseif ($post->post_type === 'tmw_category_page' && $is_manual_category_request) {
            $keyword_pack = self::bootstrap_manual_category_generate($post, $payload);
            // v5.9.0: carry the manual-generate intent into the template builder
            // so the pool guard log can report the real flag rather than a hardcoded value.
            $keyword_pack['_manual_cat_generate'] = true;
        }

        if ($keywords_only) {
            delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
            update_post_meta($post_id, '_tmwseo_optimize_done', 'keywords_refreshed');

            if ($post->post_type === 'model' && !empty($keyword_pack['primary'])) {
                AssistedDraftEnrichmentService::update_model_secondary_keywords_for_post($post, (string)$keyword_pack['primary']);
            }

            Logs::info('content', '[TMW-KEYWORDS] Refreshed keyword pack + RankMath fields only', [
                'post_id' => $post_id,
                'post_type' => $post->post_type,
                'manual_bootstrap' => $is_manual_model_request,
            ]);

            return;
        }

        // ── Architecture v5.0: Content Generation Gate ────────────
        // Preserve the gate globally, but evaluate it AFTER explicit manual
        // model bootstrap so safe prerequisites can be satisfied first.
        if ( class_exists( ContentGenerationGate::class ) ) {
            $gate = ContentGenerationGate::evaluate( $post_id );
            if ( ! $gate['allowed'] ) {
                Logs::info( 'content', '[TMW-CONTENT] Optimization blocked by ContentGenerationGate', [
                    'post_id' => $post_id,
                    'reasons' => $gate['reasons'] ?? [],
                    'manual_bootstrap' => $is_manual_model_request,
                ] );
                delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
                update_post_meta($post_id, '_tmwseo_optimize_done', 'blocked_content_gate');
                return;
            }
        }

        $model_data_gate = ($post->post_type === 'model')
            ? TemplateContent::evaluate_model_data_gate($post, $keyword_pack)
            : null;

        // ── Claude (Anthropic) strategy ──────────────────────────────────────
        if ($strategy === 'claude' && $post->post_type === 'model' && Anthropic::is_configured()) {
            $focus_kw = !empty($keyword_pack['primary'])
                ? (string)$keyword_pack['primary']
                : trim((string)get_post_meta($post_id, '_tmwseo_keyword', true));
            if ($focus_kw === '') $focus_kw = trim((string)$post->post_title);
            $focus_kw = AssistedDraftEnrichmentService::normalize_focus_keyword_for_post($post, $focus_kw);

            $claude_result = ClaudeContent::build_model($post, $keyword_pack);

            if ($claude_result['ok']) {
                $support_payload  = TemplateContent::build_model_renderer_support_payload($post, array_merge($keyword_pack, ['model_data_gate' => $model_data_gate]));
                $renderer_payload = array_merge($support_payload, $claude_result['payload']);
                $generated_content = ModelPageRenderer::render((string)$post->post_title, $renderer_payload);
                // External Profile Evidence canonical prepend (v5.8.6) — Claude assisted-draft path.
                if (class_exists( \TMWSEO\Engine\Content\ModelResearchEvidence::class )) {
                    $generated_content = \TMWSEO\Engine\Content\ModelResearchEvidence::prepend_sections( $post_id, $generated_content, (string) $post->post_title );
                }
                // Final-pass deterministic copy cleanup (v5.8.8).
                if (class_exists( \TMWSEO\Engine\Content\ModelCopyCleanup::class )) {
                    $generated_content = \TMWSEO\Engine\Content\ModelCopyCleanup::cleanup( $generated_content, (string) $post->post_title );
                }
                $generated_content = wp_kses_post($generated_content);
                // Always use canonical builder for model pages — provider titles
                // may miss a required number or sentiment word for Rank Math.
                $canon_name = trim((string)($keyword_pack['primary'] ?? $focus_kw ?: $post->post_title));
                $seo_title = TemplateContent::build_default_model_seo_title($canon_name, TemplateContent::resolve_primary_platform_label_for_title($post_id), $post_id);
                $meta_desc = TitleFixer::shorten((string)($claude_result['meta_description'] ?? ''), 160);

                $final_content = $insert_block
                    ? self::upsert_ai_block((string)$post->post_content, $generated_content)
                    : $generated_content;

                AssistedDraftEnrichmentService::persist_quality_score($post_id, $generated_content, $post, $focus_kw, $keyword_pack);

                wp_update_post(['ID' => $post_id, 'post_content' => $final_content]);

                if ($seo_title !== '') update_post_meta($post_id, 'rank_math_title', $seo_title);
                if ($meta_desc !== '') update_post_meta($post_id, 'rank_math_description', $meta_desc);
                if ($focus_kw !== '') {
                    if (!empty($keyword_pack) && class_exists('\\TMWSEO\\Engine\\Content\\RankMathMapper')) {
                        RankMathMapper::sync_to_rank_math($post_id, $keyword_pack, true);
                    } elseif (!get_post_meta($post_id, 'rank_math_focus_keyword', true)) {
                        update_post_meta($post_id, 'rank_math_focus_keyword', $focus_kw);
                    }
                    update_post_meta($post_id, '_tmwseo_keyword', $focus_kw);
                    AssistedDraftEnrichmentService::update_model_secondary_keywords_for_post($post, $focus_kw);
                }

                self::maybe_clear_rank_math_noindex($post);
                delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
                update_post_meta($post_id, '_tmwseo_optimize_done', 'claude');

                Logs::info('content', '[TMW-CLAUDE] Claude content generated', [
                    'post_id'  => $post_id,
                    'strategy' => 'claude',
                ]);
                return;
            }

            // Claude failed — log it and fall through to OpenAI or template.
            Logs::warn('content', '[TMW-CLAUDE] Claude generation failed, falling back', [
                'post_id' => $post_id,
                'error'   => $claude_result['error'] ?? 'unknown',
            ]);
            $strategy = OpenAI::is_configured() ? 'openai' : 'template';
        }

        // Template generation is the default fallback (replaces the old "offline dry" placeholder).
        if ($strategy === 'template' || !OpenAI::is_configured()) {
            $focus_kw = '';
            if ($post->post_type === 'model' && !empty($keyword_pack['primary'])) {
                $focus_kw = (string)$keyword_pack['primary'];
