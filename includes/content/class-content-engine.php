        Logs::info('content', '[TMW-CAT-CTA-GEN] appended CTA', [
            'post_id'  => (int) $post->ID,
            'term_id'  => (int) $term->term_id,
            'taxonomy' => (string) $term->taxonomy,
        ]);

        return rtrim($html) . "\n\n" . trim($cta_html);
    }

    /**
     * Ensure visible category content includes every Rank Math keyword at least once.
     *
     * @param array{primary_keyword:string,extra_keywords:string[],all_keywords:string[]} $keyword_set
     */
    private static function ensure_category_keyword_coverage(string $html, array $keyword_set, \WP_Post $post): string {
        $all_keywords = array_values(array_filter(array_map('strval', $keyword_set['all_keywords'] ?? []), 'strlen'));
        if ($html === '' || empty($all_keywords)) {
            return $html;
        }

        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $visible_lc = function_exists('mb_strtolower') ? mb_strtolower($visible, 'UTF-8') : strtolower($visible);
        $missing = [];

        foreach ($all_keywords as $keyword) {
            $keyword_lc = function_exists('mb_strtolower') ? mb_strtolower($keyword, 'UTF-8') : strtolower($keyword);
            if ($keyword_lc !== '' && strpos($visible_lc, $keyword_lc) === false) {
                $missing[] = $keyword;
            }
        }

        $fallback_used = false;
        if (!empty($missing)) {
            $fallback_used = true;
            $html = self::inject_category_keyword_fallback_sentences($html, $missing);
        }

        // Re-check after injection so logs report final missing count.
        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $visible_lc = function_exists('mb_strtolower') ? mb_strtolower($visible, 'UTF-8') : strtolower($visible);
        $remaining = 0;
        foreach ($all_keywords as $keyword) {
            $keyword_lc = function_exists('mb_strtolower') ? mb_strtolower($keyword, 'UTF-8') : strtolower($keyword);
            if ($keyword_lc !== '' && strpos($visible_lc, $keyword_lc) === false) {
                $remaining++;
            }
        }

        $term_context = self::collect_category_rank_math_term_keyword_values($post);
        Logs::info('content', '[TMW-CAT-SEO-KW] Category keyword coverage checked', [
            'term_id' => (int) ($term_context['term_id'] ?: $post->ID),
            'taxonomy' => (string) ($term_context['taxonomy'] ?: 'tmw_category_page'),
            'missing_keyword_count' => $remaining,
            'fallback_injection_used' => $fallback_used,
        ]);

        return $html;
    }

    /** @param string[] $keywords */
    private static function format_category_keyword_list(array $keywords): string {
        $formatted = [];

        foreach ($keywords as $keyword) {
            $keyword = trim((string) $keyword);
            if ($keyword !== '') {
                $formatted[] = esc_html($keyword);
            }
        }

        $count = count($formatted);
        if ($count === 0) {
            return '';
        }

        if ($count === 1) {
            return $formatted[0];
        }

        if ($count === 2) {
            return $formatted[0] . ' or ' . $formatted[1];
        }

        $last = array_pop($formatted);
        return implode(', ', $formatted) . ', or ' . $last;
    }

    /** @param string[] $missing_keywords */
    private static function inject_category_keyword_fallback_sentences(string $html, array $missing_keywords): string {
        $keyword_list = self::format_category_keyword_list($missing_keywords);
        if ($keyword_list === '') {
            return $html;
        }

        $keyword_count = 0;
        foreach ($missing_keywords as $keyword) {
            if (trim((string) $keyword) !== '') {
                $keyword_count++;
            }
        }

        if ($keyword_count === 1) {
            $paragraph = '<p>This category is also useful for visitors comparing ' . $keyword_list . ' pages from one structured directory view. Start with the profile and video links above, then use the listed platform links and related categories to narrow the browsing path.</p>';
        } else {
            $paragraph = '<p>This category is also useful for visitors comparing ' . $keyword_list . ' options from one structured directory view. Start with the profile and video links above, then use the listed platform links and related categories to narrow the browsing path without leaving the Top Models Webcam structure.</p>';
        }

        $faq_pos = stripos($html, '<h2>Frequently Asked Questions</h2>');
        if ($faq_pos !== false) {
            return substr($html, 0, $faq_pos) . $paragraph . substr($html, $faq_pos);
        }

        return rtrim($html) . $paragraph;
    }


    /**
     * Build the "Related Webcam Categories" links block from REAL sibling
     * category terms only (resolved via the CPT's `_tmw_linked_term_id`).
     * Never invents slugs. Falls back to the real /categories/ hub link
     * when no linked term or no non-empty siblings exist (also the path
     * taken in isolated unit tests, where term functions are stubbed/absent).
     *
     * Read-only: performs no writes. Debug tag: [TMW-CAT-RELATED].
     */
    private static function build_category_related_links_html(int $post_id): string {
        $items_html = '';
        $emitted    = 0;

        if (
            $post_id > 0
            && function_exists('get_post_meta')
            && function_exists('get_term')
            && function_exists('get_terms')
            && function_exists('get_term_link')
        ) {
            $term_id = (int) get_post_meta($post_id, '_tmw_linked_term_id', true);

            if ($term_id > 0) {
                $term = get_term($term_id, 'category');

                if ($term instanceof \WP_Term && $term->taxonomy === 'category') {
                    $siblings = get_terms([
                        'taxonomy'   => 'category',
                        'exclude'    => [$term_id],
                        'hide_empty' => true,
                        'number'     => 4,
                        'orderby'    => 'count',
                        'order'      => 'DESC',
                    ]);

                    if (is_array($siblings)) {
                        foreach ($siblings as $sibling) {
                            if (!($sibling instanceof \WP_Term)) {
                                continue;
                            }

                            $link = get_term_link($sibling);
                            if (!is_string($link) || $link === '') {
                                continue;
                            }

                            $items_html .= '<li><a href="' . esc_url($link) . '">' . esc_html($sibling->name) . '</a></li>';
                            $emitted++;
                        }
                    }
                }
            }
        }

        if ($items_html !== '') {
            if (defined('WP_DEBUG') && WP_DEBUG) {
                error_log('[TMW-CAT-RELATED] emitted=' . $emitted . ' post_id=' . $post_id);
            }

            return '<p>These related categories cover nearby themes on this site:</p><ul>' . $items_html . '</ul>';
        }

        if (defined('WP_DEBUG') && WP_DEBUG) {
            error_log('[TMW-CAT-RELATED] none post_id=' . $post_id);
        }

        return '<p>Browse the full <a href="' . esc_url(home_url('/categories/')) . '">categories overview</a> to find nearby themes that match the same browsing intent.</p>';
    }

