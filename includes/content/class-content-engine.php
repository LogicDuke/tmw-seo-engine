    /**
     * @param array<string,mixed> $payload
     * @return array{primary:string,additional:string[],longtail:string[],sources:array<string,mixed>}
     */
    private static function build_category_keyword_pack(\WP_Post $post, array $payload = []): array {
        $post_id = (int) $post->ID;
        $sources = [];

        $primary = trim((string) $post->post_title);
        if ($primary !== '') {
            $sources['primary'] = 'post_title';
        }

        $candidates = [];
        foreach (['rank_math_focus_keyword', '_tmwseo_keyword'] as $meta_key) {
            $raw = trim((string) get_post_meta($post_id, $meta_key, true));
            if ($raw === '') {
                continue;
            }
            $parts = array_values(array_filter(array_map('trim', explode(',', $raw)), 'strlen'));
            if ($primary === '' && !empty($parts)) {
                $primary = (string) $parts[0];
                $sources['primary'] = $meta_key;
            }
            $candidates = array_merge($candidates, $parts);
        }

        foreach (['_tmwseo_approved_keywords', '_tmwseo_category_keywords', '_tmwseo_page_keywords', '_tmwseo_keyword_cluster'] as $meta_key) {
            $raw = get_post_meta($post_id, $meta_key, true);
            $values = self::normalize_keyword_meta_values($raw);
            if ($primary === '' && !empty($values)) {
                $primary = (string) $values[0];
                $sources['primary'] = $meta_key;
            }
            $candidates = array_merge($candidates, $values);
        }

        $stored_pack_raw = get_post_meta($post_id, '_tmwseo_keyword_pack', true);
        $stored_pack = is_string($stored_pack_raw) && $stored_pack_raw !== '' ? json_decode($stored_pack_raw, true) : [];
        if (is_array($stored_pack)) {
            foreach (['primary', 'additional', 'longtail', 'rankmath_additional'] as $key) {
                $values = self::normalize_keyword_meta_values($stored_pack[$key] ?? []);
                if ($primary === '' && !empty($values)) {
                    $primary = (string) $values[0];
                    $sources['primary'] = '_tmwseo_keyword_pack.' . $key;
                }
                $candidates = array_merge($candidates, $values);
            }
        }

        $payload_keyword = trim((string)($payload['keyword'] ?? ''));
        if ($primary === '' && $payload_keyword !== '') {
            $primary = $payload_keyword;
            $sources['primary'] = 'payload';
        }
        if ($primary === '') {
            $primary = 'Webcam Category';
            $sources['primary'] = 'fallback';
        }

        $primary = class_exists(AssistedDraftEnrichmentService::class)
            ? AssistedDraftEnrichmentService::normalize_focus_keyword_for_post($post, $primary)
            : $primary;
        $candidates[] = $primary;
        $candidates = array_values(array_unique(array_filter(array_map(static function ($value): string {
            return trim((string) $value);
        }, $candidates), 'strlen')));

        $additional = array_values(array_filter($candidates, static fn(string $kw): bool => strcasecmp($kw, $primary) !== 0));
        $longtail = array_slice($additional, 0, 8);

        // v5.9.4: CategoryApprovedKeywordResolver
        // Reads status=approved rows for this category post from the keyword
        // pool DB table. Falls back to label-derived pack if none exist.
        // Never uses queued_for_review or any other non-approved status.
        $content_terms = [];
        if ( class_exists( CategoryApprovedKeywordResolver::class ) ) {
            $resolver        = new CategoryApprovedKeywordResolver();
            $resolver_result = $resolver->resolve_for_category( $post_id, $primary );
            $pool_count      = (int) ( $resolver_result['pool_count'] ?? 0 );

            if ( $pool_count > 0 ) {
                $additional               = $resolver_result['rankmath_extras'];
                $longtail                 = $additional;
                $content_terms            = $resolver_result['content_terms'];
                $sources['category_pool'] = $resolver_result['source'];

                Logs::info( 'content', sprintf(
                    '[TMW-CAT-KW] resolved post_id=%d focus=%s rankmath_extras=%s content_terms=%s pool_count=%d',
                    $post_id,
                    $primary,
                    implode( '|', $additional ),
                    implode( '|', $content_terms ),
                    $pool_count
                ) );
            } else {
                Logs::info( 'content', sprintf(
                    '[TMW-CAT-KW] no_approved_pool_terms post_id=%d fallback=label_derived',
                    $post_id
                ) );
            }
        }
        // END CategoryApprovedKeywordResolver

        // PR B (v5.9.x): deterministic Layer 2 semantic/supporting keyword fallback.
        // content_terms is normally populated above from a real DataForSEO-approved
        // keyword pool. When no approved pool exists yet for this category (the
        // common case for newer or unaudited categories), content_terms stays empty
        // and the keyword-enriched template variants render with a blank
        // {{content_terms}} placeholder. This fallback fills that gap with a fixed,
        // safe, non-explicit pool of directory/browsing terms — selected
        // deterministically per category so output is stable across re-generates —
        // without ever writing these terms as Rank Math keywords.
        if ( empty( $content_terms ) ) {
            $content_terms = self::deterministic_category_supporting_keywords( $post_id, $primary );
            $sources['supporting_keywords'] = 'deterministic_fallback_pool';

            Logs::info( 'content', sprintf(
                '[TMW-CAT-KW] supporting_keywords_fallback post_id=%d terms=%s',
                $post_id,
                implode( '|', $content_terms )
            ) );
        }

        return [
            'primary'             => $primary,
            'additional'          => array_slice( $additional, 0, 8 ),
            'rankmath_additional' => array_slice( $additional, 0, 4 ),
            'longtail'            => $longtail,
            'content_terms'       => $content_terms,
            'sources'             => $sources,
        ];
    }

    /**
     * Deterministic Layer 2 semantic/supporting keyword pool for category pages.
     *
     * These terms are NOT Rank Math focus or extra keywords. They are safe,
     * non-explicit, directory-style terms that help Google understand the page
     * topic and give the template prose natural variety. Selection is
     * deterministic per post so re-generating the same category page produces
     * the same supporting term set, while different categories get a different
     * rotation of the pool.
     *
     * @return string[] Up to 6 supporting terms, never duplicating $primary or
     *                   any keyword already present in $exclude.
     */
    private static function deterministic_category_supporting_keywords( int $post_id, string $primary, array $exclude = [] ): array {
        $pool = [
            'webcam directory',
            'model profiles',
            'video clips',
            'performer listings',
            'live cam category',
            'browsing guide',
            'platform links',
            'related categories',
            'model discovery',
            'category archive',
            'internal search',
            'profile navigation',
            'listed platforms',
            'performer index',
            'category browsing',
            'model directory',
            'video archive',
        ];

        $exclude_lc = array_map(static function (string $value): string {
            return function_exists('mb_strtolower') ? mb_strtolower(trim($value), 'UTF-8') : strtolower(trim($value));
        }, array_merge([$primary], $exclude));

        $available = array_values(array_filter($pool, static function (string $term) use ($exclude_lc): bool {
            $term_lc = function_exists('mb_strtolower') ? mb_strtolower($term, 'UTF-8') : strtolower($term);
            return !in_array($term_lc, $exclude_lc, true);
        }));

        if (empty($available)) {
            return [];
        }

        // Deterministic rotation seeded by post_id so the same category always
        // selects the same 6 terms (stable across re-generates) while different
        // categories start at a different offset in the pool.
        $count = count($available);
        $take = min(6, $count);
        $offset = $post_id % $count;

        $selected = [];
        for ($i = 0; $i < $take; $i++) {
            $selected[] = $available[($offset + $i) % $count];
        }

        return $selected;
    }

    /**
     * Build one natural sentence weaving Layer 2 supporting/semantic keywords
     * into legacy-path category prose. Used only by the legacy builder fallback
     * (the primary CategoryTemplatePool path already exposes content_terms to
     * its own keyword-enriched section variants via {{content_terms}}).
     *
     * Returns an empty string when no supporting terms are available so the
     * legacy content block is unaffected when content_terms is empty.
     */
    private static function build_category_supporting_keyword_sentence(array $keyword_pack): string {
        $terms = !empty($keyword_pack['content_terms']) && is_array($keyword_pack['content_terms'])
            ? array_slice(array_values(array_filter(array_map('strval', $keyword_pack['content_terms']))), 0, 4)
            : [];

        if (empty($terms)) {
            return '';
        }

        $term_list = implode(', ', array_map('esc_html', array_slice($terms, 0, -1)))
            . (count($terms) > 1 ? ' and ' . esc_html($terms[count($terms) - 1]) : esc_html($terms[0]));

        return '<p>This page is also useful for related browsing tasks such as ' . $term_list . ', since the same model and video links above cover those paths as well.</p>';
    }

    /**
     * @param mixed $raw
     * @return string[]
     */
    private static function normalize_keyword_meta_values($raw): array {
        if (is_string($raw)) {
            $decoded = json_decode($raw, true);
            if (is_array($decoded)) {
                $raw = $decoded;
            } else {
                return array_values(array_filter(array_map('trim', preg_split('/[,;\n]+/', $raw) ?: []), 'strlen'));
            }
        }

        if (!is_array($raw)) {
            return [];
        }

        $values = [];
        $keyword_keys = ['keyword', 'term', 'label', 'name'];
        $has_named_keyword_key = !empty(array_intersect($keyword_keys, array_filter(array_keys($raw), 'is_string')));
        foreach ($raw as $key => $value) {
            if (is_array($value)) {
                $values = array_merge($values, self::normalize_keyword_meta_values($value));
                continue;
            }
            if (is_string($key) && in_array($key, $keyword_keys, true) && is_scalar($value)) {
                $values[] = (string) $value;
                continue;
            }
            if (!$has_named_keyword_key && is_scalar($value)) {
                $values[] = (string) $value;
            }
        }

        return array_values(array_filter(array_map('trim', $values), 'strlen'));
    }

