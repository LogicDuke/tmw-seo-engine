    private static function ensure_category_keyword_coverage(string $html, array $keyword_set, \WP_Post $post): string {
        $all_keywords = array_values(array_filter(array_map('strval', $keyword_set['all_keywords'] ?? []), 'strlen'));
        if ($html === '' || empty($all_keywords)) {
            return $html;
        }

        // v5.9.7 FIX: coverage runs at build AND at save, and the density
        // reducer used to remove keywords between the two passes — so the
        // injector saw them as "missing" again and added a second batch of
        // fallback sentences (duplicated visibly on live pages). Injected
        // sentences now carry the tmw-cat-kwcov marker class and are stripped
        // before recomputing coverage, which makes the whole pass idempotent:
        // repeated runs converge on one bounded set of fallback sentences.
        $html = (string) preg_replace('/<p class="tmw-cat-kwcov">.*?<\/p>/isu', '', $html);

        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $visible_lc = function_exists('mb_strtolower') ? mb_strtolower($visible, 'UTF-8') : strtolower($visible);
        $missing = [];

        foreach ($all_keywords as $keyword) {
            $keyword_lc = function_exists('mb_strtolower') ? mb_strtolower($keyword, 'UTF-8') : strtolower($keyword);
            if ($keyword_lc !== '' && strpos($visible_lc, $keyword_lc) === false) {
                $missing[] = $keyword;
            }
        }

        $fallback_used = false;
        if (!empty($missing)) {
            $fallback_used = true;
            $html = self::inject_category_keyword_fallback_sentences($html, $missing, $post);
        }

        // v5.9.7: tracking-only keywords are intentionally absent from body
        // copy (they stay in the Rank Math CSV). Log them once so a missing
        // exact variant is visibly a decision, not a coverage bug.
        $intentionally_unused = (array) ($keyword_set['unused_keywords'] ?? []);
        if (!empty($intentionally_unused)) {
            Logs::info('content', '[TMW-CAT-SEO-KW] tracking-only keywords intentionally not in body', [
                'post_id' => (int) $post->ID,
                'unused'  => $intentionally_unused,
            ]);
        }

        // v5.9.x: supporting keyword coverage from the plugin extra keyword pool.
        // Runs after Rank Math coverage so the guaranteed keyword set is untouched.
        //
        // v5.9.5 ROOT-CAUSE FIX: the weave frames its terms as things visitors
        // search for ("Popular ways to explore this archive include …").
        // That framing is only truthful for REAL approved search phrases from
        // the category keyword pool. The deterministic fallback vocabulary
        // ("performer listings", "live cam category", "model directory", …)
        // is internal navigation shorthand — presenting it as user search
        // terms produced the nonsense sentences documented in the audit PDF.
        // The weave therefore only runs when the supporting terms came from
        // the approved pool; fallback terms are never framed as searches.
        $supporting        = array_values(array_filter(array_map('strval', $keyword_set['supporting_keywords'] ?? []), 'strlen'));
        $supporting_source = (string) ($keyword_set['supporting_source'] ?? '');
        if (!empty($supporting) && $supporting_source === 'category_db_approved') {
            $html = self::weave_category_supporting_keywords($html, $supporting, $post);
        } elseif (!empty($supporting)) {
            Logs::info('content', '[TMW-CAT-SEO-KW] supporting-term weave skipped (fallback vocabulary is not user search phrasing)', [
                'post_id' => (int) $post->ID,
                'source'  => $supporting_source !== '' ? $supporting_source : 'unknown',
            ]);
        }

        // v5.8.32: cap exact focus-keyword repetition so Rank Math density
        // stays safe and the page reads naturally. Runs before the final
        // re-check so the missing-count log reflects the shipped content.
        $primary = trim((string) ($keyword_set['primary_keyword'] ?? ''));
        if ($primary !== '') {
            $html = self::reduce_category_focus_keyword_density($html, $primary, $post);
            $html = self::reduce_category_root_family_density($html, $primary, (array) ($keyword_set['extra_keywords'] ?? []), (array) ($keyword_set['body_use_keywords'] ?? $supporting), $post);
        }

        // v5.9.5: final deterministic copy guard — repairs any surviving
        // implementation-language phrase or article/possessive collision.
        if (class_exists('\\TMWSEO\\Engine\\Content\\CategoryCopyGuard')) {
            $html = CategoryCopyGuard::cleanup($html, (int) $post->ID);
        }

        // Re-check after injection so logs report final missing count.
        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $visible_lc = function_exists('mb_strtolower') ? mb_strtolower($visible, 'UTF-8') : strtolower($visible);
        $remaining = 0;
        foreach ($all_keywords as $keyword) {
            $keyword_lc = function_exists('mb_strtolower') ? mb_strtolower($keyword, 'UTF-8') : strtolower($keyword);
            if ($keyword_lc !== '' && strpos($visible_lc, $keyword_lc) === false) {
                $remaining++;
            }
        }

        $term_context = self::collect_category_rank_math_term_keyword_values($post);
        Logs::info('content', '[TMW-CAT-SEO-KW] Category keyword coverage checked', [
            'term_id' => (int) ($term_context['term_id'] ?: $post->ID),
            'taxonomy' => (string) ($term_context['taxonomy'] ?: 'tmw_category_page'),
            'missing_keyword_count' => $remaining,
            'fallback_injection_used' => $fallback_used,
        ]);

        return $html;
    }


    /**
     * Keep combined exact variants from the primary root family below the hard
     * 2.2% ceiling by replacing non-selected extras with natural paraphrases.
     *
     * @param string[] $extras
     * @param string[] $body_use
     */
    private static function reduce_category_root_family_density(string $html, string $primary, array $extras, array $body_use, \WP_Post $post): string {
        if ($html === '' || $primary === '') { return $html; }
        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $words = max(1, str_word_count($visible));
        $family = self::category_keyword_root_family($primary);
        $family_terms = [];
        foreach (array_merge([$primary], $extras) as $term) {
            $term = trim((string) $term);
            if ($term !== '' && self::category_keyword_root_family($term) === $family) { $family_terms[] = $term; }
        }
        $hits = 0;
        foreach (array_unique($family_terms) as $term) { $hits += preg_match_all('/(?<![\p{L}\p{N}])' . preg_quote($term, '/') . '(?![\p{L}\p{N}])/iu', $visible) ?: 0; }
        if (($hits / $words) * 100 <= 2.2) { return $html; }

        $body_keys = [];
        foreach ($body_use as $term) { $body_keys[strtolower(trim((string) $term))] = true; }
        $replace_terms = array_values(array_filter($extras, static function ($term) use ($body_keys): bool {
            $key = strtolower(trim((string) $term));
            return $key !== '' && empty($body_keys[$key]);
        }));
        if (empty($replace_terms)) { return $html; }

        $parts = preg_split('/(<[^>]+>)/', $html, -1, PREG_SPLIT_DELIM_CAPTURE);
        if (!is_array($parts)) { return $html; }

        // v5.9.7 FIX: the previous implementation substituted excess keyword
        // occurrences with literal analysis-vocabulary placeholders ("related
        // room-browsing intent", "similar public cam-room searches", "nearby
        // cam-room queries") — the exact broken phrases that reached live
        // category pages. Excess occurrences are now replaced with neutral
        // page references, and ONLY in grammar-safe positions: the match must
        // be followed by end-of-node, punctuation, or a common connector, so
        // attributive uses ("<keyword> performers") are never mangled. The
        // first occurrence of each term is always kept so a later coverage
        // pass never re-injects the term (the coverage/density ping-pong that
        // duplicated fallback sentences). Anything this conservative pass
        // cannot safely reduce is left for the pipeline quality guard.
        $replaced   = 0;
        $kept_first = [];
        $alts       = ['this category', 'this archive', 'these listings'];
        $safe_tail  = '(?=\s*$|\s*[.,;:!?)]|\s+(?:and|or|is|are|was|were|can|could|will|would|has|have|had|also|too|as|when|while|because|so|but|which|that|here|there|now|today)\b)';
        foreach ($parts as $i => $part) {
            if ($part === '' || ($part[0] ?? '') === '<') { continue; }
            foreach ($replace_terms as $term) {
                $term_key = strtolower(trim((string) $term));
                $pattern  = '/(?<![\p{L}\p{N}])' . preg_quote((string) $term, '/') . '(?![\p{L}\p{N}])' . $safe_tail . '/iu';
                $parts[$i] = (string) preg_replace_callback($pattern, static function ($m) use (&$replaced, &$kept_first, $term_key, $alts) {
                    if (empty($kept_first[$term_key])) {
                        $kept_first[$term_key] = true;
                        return $m[0];
                    }
                    $replacement = $alts[$replaced % count($alts)];
                    $replaced++;
                    return $replacement;
                }, $parts[$i]);
            }
        }
        if ($replaced > 0) {
            Logs::info('content', '[TMW-CAT-SEO-KW] root-family density reduced', ['post_id' => (int) $post->ID, 'root_family' => $family, 'replaced' => $replaced]);
        }
        return implode('', $parts);
    }

    /**
     * v5.8.32: reduce exact focus-keyword repetition in category body text.
     *
     * Pool templates and the FAQ pool each carry the {{category_name}}
     * placeholder, so an assembled page can repeat the exact focus keyword
     * 15–20+ times (Rank Math flagged 21 occurrences / 2.54% density for
     * an audited trait category). This pass keeps the keyword where it matters — first
     * occurrence (intro), first occurrence inside the FAQ block, and the
     * final occurrence (closing) — and rewrites the other exact occurrences
     * with rotating neutral phrases ("this category", "this archive", ...).
     *
     * Grammar safety: a match is only replaced when the word that follows it
     * is punctuation, a tag boundary, or a common verb/connector — so
     * attributive uses like "<category name> performers" are left untouched
     * rather than mangled. Only text nodes are rewritten; tags, attributes,
     * URLs, and block comments are never modified, so links and the CTA
     * block survive unchanged. If the page already uses the keyword 5 times
     * or fewer, nothing is changed (which also makes the pass idempotent).
     */
    private static function reduce_category_focus_keyword_density(string $html, string $focus, \WP_Post $post): string {
        $focus = trim($focus);
        if ($html === '' || $focus === '') {
            return $html;
        }

        $max_kept_before_reduction = 5;
        $pattern = '/(?<![\p{L}\p{N}])' . preg_quote($focus, '/') . '(?![\p{L}\p{N}])/iu';

        $parts = preg_split('/(<[^>]+>)/', $html, -1, PREG_SPLIT_DELIM_CAPTURE);
        if (!is_array($parts)) {
            return $html;
        }

        // First pass: locate every exact occurrence in text nodes only.
        // Text inside <h2> section headings is exempt from replacement —
        // structural headings like "About {keyword}" keep the keyword (the
        // "one heading where natural" slot); FAQ <h3> questions stay reducible.
        $occurrences = []; // each: [part_index, offset, length, in_h2]
        $faq_seen = false;
        $faq_first_occurrence = -1;
        $in_h2 = false;
        foreach ($parts as $i => $part) {
            if ($part === '' || ($part[0] ?? '') === '<') {
                if (preg_match('/^<h2\b/i', $part)) {
                    $in_h2 = true;
                } elseif (preg_match('/^<\/h2/i', $part)) {
                    $in_h2 = false;
                }
                continue;
            }
            if (!$faq_seen && stripos($part, 'Frequently Asked Questions') !== false) {
                $faq_seen = true;
                $faq_first_occurrence = count($occurrences); // next match index is FAQ's first
            }
            if (preg_match_all($pattern, $part, $m, PREG_OFFSET_CAPTURE)) {
                foreach ($m[0] as $hit) {
                    $occurrences[] = [$i, (int) $hit[1], strlen($hit[0]), $in_h2];
                }
            }
        }

        $total = count($occurrences);
        if ($total <= $max_kept_before_reduction) {
            return $html;
        }

        $keep = [0, $total - 1];
        if ($faq_first_occurrence >= 0 && $faq_first_occurrence < $total) {
            $keep[] = $faq_first_occurrence;
        }
        $keep = array_flip($keep);

        // v5.9.5: alternatives no longer include implementation-language
        // phrases ("this webcam theme", "this browsing theme") that leaked
        // internal vocabulary into visitor copy. 'as' and 'content' are also
        // removed from the follow-word allowlist: replacing before "as a
        // category" produced "This page as a category …" and replacing before
        // "content" produced "… its this webcam theme content".
        $alternatives = ['this category', 'this archive', 'this page', 'this collection', 'this section'];
        $safe_follow = [
            'is', 'are', 'was', 'were', 'and', 'or', 'also', 'remains', 'works',
            'covers', 'offers', 'keeps', 'gives', 'collects', 'provides', 'includes',
            'sits', 'stays', 'helps', 'serves', 'features', 'matches', 'leads',
            'connects', 'lets', 'allows', 'can', 'will', 'does', 'has', 'have',
            'may', 'should', 'would', 'in', 'on', 'for', 'with', 'from', 'to', 'at',
        ];
        // Never substitute "this …" straight after an article/possessive —
        // "the <category name> archive" must not become "the this category archive".
        $blocked_preceding = [
            'the', 'a', 'an', 'its', 'their', 'our', 'your', 'this', 'that',
            'each', 'every', 'his', 'her',
        ];

        $replaced = 0;
        $rotation = 0;
        // Replace from the end so recorded offsets stay valid.
        for ($idx = $total - 1; $idx >= 0; $idx--) {
            if (isset($keep[$idx])) {
                continue;
            }
            [$part_index, $offset, $length, $occ_in_h2] = $occurrences[$idx];
            if ($occ_in_h2) {
                continue; // structural <h2> headings keep the keyword
            }
            $text = $parts[$part_index];
            $after = ltrim(substr($text, $offset + $length));

            $follow_ok = false;
            if ($after === '') {
                $follow_ok = true; // tag boundary follows (end of text node)
            } elseif (preg_match('/^[.,;:!?)\'"\x{2019}\x{201D}]/u', $after)) {
                $follow_ok = true;
            } elseif (preg_match('/^([a-z]+)/i', $after, $fm) && in_array(strtolower($fm[1]), $safe_follow, true)) {
                $follow_ok = true;
            }
            if (!$follow_ok) {
                continue; // attributive use ("... performers") — leave intact
            }

            // v5.9.5: preceding-word guard against article/possessive collisions.
            $before_probe = rtrim(substr($text, 0, $offset));
            if (preg_match('/([A-Za-z\x{2019}\']+)$/u', $before_probe, $pm)
                && in_array(strtolower(str_replace(["\u{2019}", "'"], '', $pm[1])), $blocked_preceding, true)) {
                continue; // "the/its/their <keyword> …" — replacement would collide
            }

            $replacement = $alternatives[$rotation % count($alternatives)];
            $rotation++;

            // Capitalize at sentence/segment start.
            $before = substr($text, 0, $offset);
            $before_trim = rtrim($before);
            if ($before_trim === '' || preg_match('/[.!?]$/', $before_trim)) {
                $replacement = ucfirst($replacement);
            }

            $parts[$part_index] = substr($text, 0, $offset) . $replacement . substr($text, $offset + $length);
            $replaced++;
        }

        if ($replaced === 0) {
            return $html;
        }

        $result = implode('', $parts);

        Logs::info('content', '[TMW-CAT-SEO-KW] focus keyword density reduced', [
            'post_id' => (int) $post->ID,
            'focus' => $focus,
            'occurrences_before' => $total,
            'replaced' => $replaced,
            'occurrences_after' => $total - $replaced,
        ]);

        return $result;
    }

    /** @param string[] $keywords */
    private static function format_category_keyword_list(array $keywords): string {
        $formatted = [];

        foreach ($keywords as $keyword) {
            $keyword = trim((string) $keyword);
            if ($keyword !== '') {
                $formatted[] = esc_html($keyword);
            }
        }

        $count = count($formatted);
        if ($count === 0) {
            return '';
        }

        if ($count === 1) {
            return $formatted[0];
        }

        if ($count === 2) {
            return $formatted[0] . ' or ' . $formatted[1];
        }

        $last = array_pop($formatted);
        return implode(', ', $formatted) . ', or ' . $last;
    }

    /**
     * Guarantee every saved Rank Math keyword appears in the visible copy.
     *
     * v5.9.5: rewritten to DISTRIBUTE the missing keywords across the page
     * instead of dumping them into one comma-separated sentence.
     * v5.9.7: hardened for the universal pipeline era. Rules:
     *  - never more than TWO exact keyword phrases in any single sentence;
     *  - at most TWO fallback sentences per page (four keywords) — anything
     *    beyond that stays tracking-only and is logged by the caller;
     *  - every injected sentence carries the tmw-cat-kwcov marker class so
     *    the coverage pass is idempotent across build + save runs;
     *  - the template rotation is seeded per category slug so different
     *    categories don't share one fallback formula.
     *
     * @param string[] $missing_keywords
     */
    private static function inject_category_keyword_fallback_sentences(string $html, array $missing_keywords, \WP_Post $post): string {
        $missing = array_values(array_filter(array_map(static function ($kw): string {
            return trim((string) $kw);
        }, $missing_keywords), 'strlen'));

        if (empty($missing) || $html === '') {
            return $html;
        }

        $missing = array_slice($missing, 0, 4);
        $pairs   = array_slice(array_chunk($missing, 2), 0, 2);

        $templates = [
            '<p class="tmw-cat-kwcov">Visitors comparing %s can use this page to look for active public cam rooms, then open individual listings to check whether a performer, clip, or platform fit is relevant.</p>',
            '<p class="tmw-cat-kwcov">Search intent around %s is best handled with practical checks: public-room access, private or paid feature labels, and whether a listing clearly matches the category before you click.</p>',
            '<p class="tmw-cat-kwcov">If you arrived looking for %s, compare the visible performer and video details first, and avoid assuming that every feature on a linked platform is fully free.</p>',
            '<p class="tmw-cat-kwcov">People exploring %s should review room activity, privacy expectations, and platform prompts before sharing information or moving into private features.</p>',
        ];

        // Per-category rotation offset so pages don't all open their fallback
        // coverage with the same formula.
        $seed_source = (string) $post->post_name !== '' ? (string) $post->post_name : (string) $post->ID;
        $offset      = abs(crc32($seed_source . '|kwcov')) % count($templates);

        $sentences = [];
        foreach ($pairs as $i => $pair) {
            $sentences[] = sprintf($templates[($offset + $i) % count($templates)], self::format_category_keyword_list($pair));
        }

        // 1) First sentence after the opening paragraph.
        $first = array_shift($sentences);
        if ($first !== null) {
            $first_p_end = stripos($html, '</p>');
            if ($first_p_end !== false) {
                $insert_at = $first_p_end + 4;
                $html = substr($html, 0, $insert_at) . $first . substr($html, $insert_at);
            } else {
                $html = $first . $html;
            }
        }

        // 2) Middle sentence before the third <h2> (roughly mid-page).
        $second = array_shift($sentences);
        if ($second !== null) {
            $h2_positions = [];
            if (preg_match_all('/<h2\b/i', $html, $m, PREG_OFFSET_CAPTURE)) {
                foreach ($m[0] as $hit) {
                    $h2_positions[] = (int) $hit[1];
                }
            }
            $mid_pos = $h2_positions[2] ?? ($h2_positions[1] ?? null);
            if ($mid_pos !== null) {
                $html = substr($html, 0, $mid_pos) . $second . substr($html, $mid_pos);
            } else {
                $html = rtrim($html) . $second;
            }
        }

        // 3) Remaining sentences before the FAQ block (or appended).
        foreach ($sentences as $sentence) {
            $faq_pos = stripos($html, '<h2>Frequently Asked Questions</h2>');
            if ($faq_pos !== false) {
                $html = substr($html, 0, $faq_pos) . $sentence . substr($html, $faq_pos);
            } else {
                $html = rtrim($html) . $sentence;
            }
        }

        return $html;
    }

    /**
     * v5.9.x: weave supporting keywords from the plugin extra keyword pool
     * into category content, without keyword stuffing.
     *
     * Rules:
     *  - Insertion cap scales with visible length: 2 (<450 words),
     *    3 (<700 words), 4 otherwise.
     *  - Terms already present in the visible text are never repeated.
     *  - At most two short neutral sentences are added (early + before FAQ),
     *    each carrying up to two terms — no lists, no links, no slugs.
     *  - The tmw-cat-supporting marker class makes the weave idempotent, so
     *    repeated coverage passes on the same content never stack sentences.
     *
     * @param string[] $supporting
     */
    private static function weave_category_supporting_keywords(string $html, array $supporting, \WP_Post $post): string {
        if ($html === '' || empty($supporting)) {
            return $html;
        }

        // Idempotency guard: only one weave per generated content string.
        if (strpos($html, 'tmw-cat-supporting') !== false) {
            return $html;
        }

        $visible = html_entity_decode(wp_strip_all_tags($html), ENT_QUOTES, 'UTF-8');
        $visible_lc = function_exists('mb_strtolower') ? mb_strtolower($visible, 'UTF-8') : strtolower($visible);
        $word_count = str_word_count($visible);

        $cap = 4;
        if ($word_count < 450) {
            $cap = 2;
        } elseif ($word_count < 700) {
            $cap = 3;
        }

        $missing = [];
        foreach ($supporting as $term) {
            $term = trim($term);
            if ($term === '') {
                continue;
            }
            $term_lc = function_exists('mb_strtolower') ? mb_strtolower($term, 'UTF-8') : strtolower($term);
            if (strpos($visible_lc, $term_lc) !== false) {
                continue; // Already covered naturally — never repeat.
            }
            $missing[] = $term;
            if (count($missing) >= $cap) {
                break;
            }
        }

        if (empty($missing)) {
            return $html;
        }

        $early_terms = array_slice($missing, 0, 2);
        $late_terms  = array_slice($missing, 2, 2);

        $early_sentence = '<p class="tmw-cat-supporting">Visitors comparing '
            . self::format_category_keyword_list($early_terms)
            . ' should look for active public rooms, clear free-access labels, and any prompts that move into private or paid features.</p>';

        $late_sentence = '';
        if (!empty($late_terms)) {
            $late_sentence = '<p class="tmw-cat-supporting">For '
                . self::format_category_keyword_list($late_terms)
                . ', compare performer details, video context, and platform expectations instead of assuming every listing offers the same free features.</p>';
        }

        // Early sentence lands after the first paragraph so it reads as intro
        // context; the late sentence lands before the FAQ block.
        $first_p_end = stripos($html, '</p>');
        if ($first_p_end !== false) {
            $insert_at = $first_p_end + 4;
            $html = substr($html, 0, $insert_at) . $early_sentence . substr($html, $insert_at);
        } else {
            $html = $early_sentence . $html;
        }

        if ($late_sentence !== '') {
            $faq_pos = stripos($html, '<h2>Frequently Asked Questions</h2>');
            if ($faq_pos !== false) {
                $html = substr($html, 0, $faq_pos) . $late_sentence . substr($html, $faq_pos);
            } else {
                $html = rtrim($html) . $late_sentence;
            }
        }

        Logs::info('content', '[TMW-CAT-SEO-KW] Supporting keyword coverage woven', [
            'post_id' => (int) $post->ID,
            'inserted_terms' => count($missing),
            'insertion_cap' => $cap,
            'word_count' => $word_count,
        ]);

        return self::repair_category_keyword_dump_patterns($html, array_merge($supporting, $missing));
    }

    /**
     * Repair deterministic keyword-dump patterns after coverage weaving.
     *
     * @param string[] $keywords
     */
    private static function repair_category_keyword_dump_patterns(string $html, array $keywords): string {
        if ($html === '' || empty($keywords)) { return $html; }
        $keywords = array_values(array_filter(array_map('trim', array_map('strval', $keywords)), 'strlen'));
        $parts = preg_split('/(<p[^>]*>|<\/p>)/i', $html, -1, PREG_SPLIT_DELIM_CAPTURE);
        if (!is_array($parts)) { return $html; }
        $out = '';
        for ($i = 0; $i < count($parts); $i++) {
            $part = $parts[$i];
            if (preg_match('/^<p/i', $part) && isset($parts[$i+1], $parts[$i+2]) && preg_match('/^<\/p>/i', $parts[$i+2])) {
                $text = wp_strip_all_tags($parts[$i+1]);
                $exact = 0;
                foreach ($keywords as $kw) {
                    if (preg_match('/(?<![\p{L}\p{N}])' . preg_quote($kw, '/') . '(?![\p{L}\p{N}])/iu', $text)) { $exact++; }
                }
                if ($exact >= 3 || preg_match('/\b[^.?!]*\b(?:cam|webcam)[^.!?]*,\s*(?:free\s+)?(?:cam|webcam)/iu', $text)) {
                    $out .= $part . 'Use the category as a starting point for active public rooms, then compare performer details, video context, privacy expectations, and any paid or private prompts before clicking through.' . $parts[$i+2];
                    $i += 2;
                    continue;
                }
            }
            $out .= $part;
        }
        return $out;
    }

