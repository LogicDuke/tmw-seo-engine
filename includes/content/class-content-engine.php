    /**
     * Evaluate whether the CategoryTemplatePool has sufficient data to produce
     * usable output for this category page.
     *
     * Returns ['sufficient' => bool, 'reasons' => string[]]
     *
     * @param \WP_Post            $post
     * @param array<string,mixed> $category_data
     * @param array<string,mixed> $keyword_pack
     * @return array{sufficient:bool,reasons:string[]}
     */
    private static function evaluate_category_template_pool_gate(\WP_Post $post, array $category_data, array $keyword_pack): array {
        $reasons = [];

        if ($post->post_type !== 'tmw_category_page') {
            $reasons[] = 'post_type_not_tmw_category_page';
        }

        if (trim((string) ($category_data['category_name'] ?? '')) === '') {
            $reasons[] = 'category_name_empty';
        }

        if (trim((string) ($category_data['focus_keyword'] ?? '')) === '') {
            $reasons[] = 'focus_keyword_empty';
        }

        if (! class_exists('\\TMWSEO\\Engine\\Content\\CategoryTemplatePool')) {
            $reasons[] = 'CategoryTemplatePool_class_missing';
            return ['sufficient' => false, 'reasons' => $reasons];
        }

        $pool          = new \TMWSEO\Engine\Content\CategoryTemplatePool();
        $section_count = count($pool->get_section_keys());
        $faq_count     = count($pool->get_faq_bucket_keys());

        if ($section_count < 3) {
            $reasons[] = 'pool_sections_insufficient:' . $section_count;
        }

        if ($faq_count < 2) {
            $reasons[] = 'pool_faq_buckets_insufficient:' . $faq_count;
        }

        // Quick resolved sample check — test one section for unresolved placeholders.
        if (empty($reasons)) {
            $post_id       = (int) $post->ID;
            $intro_section = $pool->get_section('intro', $post_id, $category_data);
            if ($intro_section !== null && $pool->has_unresolved_placeholders((string) ($intro_section['body'] ?? ''))) {
                $reasons[] = 'intro_section_has_unresolved_placeholders';
            }
        }

        return [
            'sufficient' => empty($reasons),
            'reasons'    => $reasons,
        ];
    }

    /**
     * Assemble the final category HTML from resolved pool sections and FAQs.
     *
     * Section order is fixed to match the accepted output structure:
     *   intro → what_this_category_covers → who_this_category_is_for →
     *   how_to_browse → live_cam_and_model_discovery_tips →
     *   similar_categories → what_to_check_before_opening_profile →
     *   internal_navigation (nav) → FAQ → closing_context
     *
     * Rules enforced:
     *  - No H1 injected.
     *  - One FAQ block only.
     *  - No duplicate H2 headings.
     *  - No duplicate FAQ questions.
     *
     * @param array<string,array> $sections
     * @param array               $faqs
     * @param array<string,mixed> $category_data
     * @return string
     */
    private static function build_category_pool_html(array $sections, array $faqs, array $category_data): string {
        $ordered_keys = [
            'intro',
            'what_this_category_covers',
            'who_this_category_is_for',
            'how_to_browse',
            'live_cam_and_model_discovery_tips',
            'similar_categories',
            'what_to_check_before_opening_profile',
            'internal_navigation',
        ];

        $html       = '';
        $seen_h2    = [];

        foreach ($ordered_keys as $key) {
            if (! isset($sections[$key])) {
                continue;
            }

            $sec  = $sections[$key];
            $h2   = trim((string) ($sec['h2']   ?? ''));
            $body = trim((string) ($sec['body']  ?? $sec['content'] ?? ''));

            if ($body === '') {
                continue;
            }

            // Deduplicate H2 headings across sections.
            if ($h2 !== '') {
                $h2_key = strtolower($h2);
                if (isset($seen_h2[$h2_key])) {
                    continue;
                }
                $seen_h2[$h2_key] = true;
                $html .= '<h2>' . esc_html($h2) . '</h2>';
            }

            $html .= $body;
        }

        // FAQ block — one only, no duplicate questions.
        if (! empty($faqs)) {
            $html .= '<h2>Frequently Asked Questions</h2>';
            $seen_q = [];
            $faq_index = 0;
            $primary_keyword = trim((string) ($category_data['primary_keyword'] ?? $category_data['focus_keyword'] ?? $category_data['category_name'] ?? ''));
            foreach ($faqs as $faq) {
                $q = trim((string) ($faq['q'] ?? ''));
                $a = trim((string) ($faq['a'] ?? ''));
                if ($q === '' || $a === '') {
                    continue;
                }
                $q_key = strtolower($q);
                if (isset($seen_q[$q_key])) {
                    continue;
                }
                $seen_q[$q_key] = true;
                if ($faq_index === 0 && $primary_keyword !== '' && stripos($a, $primary_keyword) === false) {
                    $a = rtrim($a) . ' This ' . $primary_keyword . ' archive keeps the path focused on directory navigation and linked listings.';
                }
                $html .= '<h3>' . esc_html($q) . '</h3><p>' . esc_html($a) . '</p>';
                $faq_index++;
            }
        }

        // Closing context if present.
        if (isset($sections['closing_context'])) {
            $body = trim((string) ($sections['closing_context']['body'] ?? $sections['closing_context']['content'] ?? ''));
            if ($body !== '') {
                $primary_keyword = trim((string) ($category_data['primary_keyword'] ?? $category_data['focus_keyword'] ?? $category_data['category_name'] ?? ''));
                if ($primary_keyword !== '' && stripos(wp_strip_all_tags($body), $primary_keyword) === false) {
                    $body .= '<p>The ' . esc_html($primary_keyword) . ' archive remains a neutral starting point for visitors who want to continue through internal model and video listings.</p>';
                }
                $html .= $body;
            }
        }

        return $html;
    }

    /**
     * Normalize the full Rank Math keyword set for category body generation.
     *
     * @param array<string,mixed> $keyword_pack
     * @return array{primary_keyword:string,extra_keywords:string[],all_keywords:string[]}
     */
    private static function normalize_category_content_keyword_set(\WP_Post $post, string $focus_kw, array $keyword_pack): array {
        $post_id = (int) $post->ID;
        $term_context = self::collect_category_rank_math_term_keyword_values($post);
        $raw_values = $term_context['values'];

        foreach ([
            (string) get_post_meta($post_id, 'rank_math_focus_keyword', true),
            (string) get_post_meta($post_id, 'rank_math_additional_keywords', true),
            $focus_kw,
            (string) ($keyword_pack['primary'] ?? ''),
        ] as $raw) {
            if (trim($raw) !== '') {
                $raw_values[] = $raw;
            }
        }

        foreach (['rankmath_additional', 'additional', 'secondary'] as $key) {
            if (!empty($keyword_pack[$key]) && is_array($keyword_pack[$key])) {
                foreach ($keyword_pack[$key] as $value) {
                    $raw_values[] = (string) $value;
                }
            }
        }

        $keywords = [];
        $seen = [];
        foreach ($raw_values as $raw) {
            foreach (preg_split('/[,;\n]+/', (string) $raw) ?: [] as $part) {
                $keyword = trim(wp_strip_all_tags((string) $part));
                if ($keyword === '') {
                    continue;
                }
                $key = function_exists('mb_strtolower') ? mb_strtolower($keyword, 'UTF-8') : strtolower($keyword);
                if (isset($seen[$key])) {
                    continue;
                }
                $seen[$key] = true;
                $keywords[] = $keyword;
            }
        }

        $primary = $keywords[0] ?? trim((string) $post->post_title);
        $extras = array_values(array_filter($keywords, static function (string $keyword) use ($primary): bool {
            return strcasecmp($keyword, $primary) !== 0;
        }));
        $extras = array_slice($extras, 0, 4);
        $all = array_values(array_merge($primary !== '' ? [$primary] : [], $extras));

        Logs::info('content', '[TMW-CAT-SEO-KW] Category keyword set built', [
            'term_id' => (int) ($term_context['term_id'] ?: $post_id),
            'taxonomy' => (string) ($term_context['taxonomy'] ?: 'tmw_category_page'),
            'primary_keyword' => $primary,
            'extra_keyword_count' => count($extras),
            'total_keywords' => count($all),
        ]);

        return [
            'primary_keyword' => $primary,
            'extra_keywords' => $extras,
            'all_keywords' => $all,
        ];
    }

    /**
     * Collect Rank Math keyword values from the real category/tag term connected
     * to a tmw_category_page draft before falling back to post meta.
     *
     * @return array{values:string[],term_id:int,taxonomy:string}
     */
    private static function collect_category_rank_math_term_keyword_values(\WP_Post $post): array {
        $term = self::resolve_category_content_term($post);
        if (! $term instanceof \WP_Term || ! function_exists('get_term_meta')) {
            return [
                'values' => [],
                'term_id' => 0,
                'taxonomy' => '',
            ];
        }

        $values = [];
        foreach (['rank_math_focus_keyword', '_rank_math_focus_keyword', 'rank_math_additional_keywords'] as $meta_key) {
            $raw = get_term_meta((int) $term->term_id, $meta_key, true);
            if (is_scalar($raw) && trim((string) $raw) !== '') {
                $values[] = (string) $raw;
            }
        }

        return [
            'values' => $values,
            'term_id' => (int) $term->term_id,
            'taxonomy' => (string) $term->taxonomy,
        ];
    }

    /**
     * Resolve the canonical taxonomy term for a tmw_category_page when available.
     *
     * The theme bridge usually maps category-page posts to real category archives
     * by slug/title. Some installs also persist a target term ID in post meta, so
     * this checks explicit term IDs before slug/title lookups.
     */
    private static function resolve_category_content_term(\WP_Post $post): ?\WP_Term {
        if ($post->post_type !== 'tmw_category_page') {
            return null;
        }

        $taxonomies = array_values(array_filter(['category', 'post_tag'], static function (string $taxonomy): bool {
            return ! function_exists('taxonomy_exists') || taxonomy_exists($taxonomy);
        }));

        foreach (['_tmwseo_term_id', '_tmwseo_category_term_id', '_tmwseo_target_term_id', 'target_term_id'] as $meta_key) {
            $term_id = (int) get_post_meta((int) $post->ID, $meta_key, true);
            if ($term_id <= 0) {
                continue;
            }
            foreach ($taxonomies as $taxonomy) {
                if (! function_exists('get_term')) {
                    continue;
                }
                $term = get_term($term_id, $taxonomy);
                if ($term instanceof \WP_Term) {
                    return $term;
                }
            }
        }

        $slug = trim((string) $post->post_name);
        if ($slug !== '' && function_exists('get_term_by')) {
            foreach ($taxonomies as $taxonomy) {
                $term = get_term_by('slug', $slug, $taxonomy);
                if ($term instanceof \WP_Term) {
                    return $term;
                }
            }
        }

        $title = trim((string) $post->post_title);
        if ($title !== '' && function_exists('get_term_by')) {
            foreach ($taxonomies as $taxonomy) {
                $term = get_term_by('name', $title, $taxonomy);
                if ($term instanceof \WP_Term) {
                    return $term;
                }
            }
        }

        return null;
    }

    /**
     * Append the term affiliate CTA to generated category-page content.
     *
     * This runs on the final generated HTML string immediately before it is
     * persisted to tmw_category_page post_content, keeping the CTA inside the
     * generated text and after the FAQ/closing paragraph.
     */
    private static function append_category_affiliate_cta_html(string $html, \WP_Post $post): string {
        if ($post->post_type !== 'tmw_category_page') {
            return $html;
        }

        if (strpos($html, 'tmw-category-page-affiliate-cta') !== false) {
            Logs::info('content', '[TMW-CAT-CTA-GEN] skipped existing CTA marker', [
                'post_id' => (int) $post->ID,
            ]);
            return $html;
        }

        $term = self::resolve_category_content_term($post);
        if (! $term instanceof \WP_Term) {
            Logs::info('content', '[TMW-CAT-CTA-GEN] skipped no valid WP_Term context', [
                'post_id' => (int) $post->ID,
            ]);
            return $html;
        }

        $cta_html = '';
        if (function_exists('tmwseo_get_category_affiliate_cta_html')) {
            $cta_html = (string) tmwseo_get_category_affiliate_cta_html($term);
        } elseif (function_exists('tmwseo_get_category_affiliate_url')) {
            $affiliate_url = (string) tmwseo_get_category_affiliate_url($term);
            if ($affiliate_url !== '') {
                $cta_html = '<div class="tmw-category-page-affiliate-cta"><a href="' . esc_url($affiliate_url) . '" target="_blank" rel="sponsored noopener">Visit live category related models</a></div>';
            }
        }

        if (trim($cta_html) === '') {
            Logs::info('content', '[TMW-CAT-CTA-GEN] skipped no affiliate URL', [
                'post_id'  => (int) $post->ID,
                'term_id'  => (int) $term->term_id,
                'taxonomy' => (string) $term->taxonomy,
            ]);
