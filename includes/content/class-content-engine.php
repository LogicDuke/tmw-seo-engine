            $strategy = 'claude';
        } elseif ($requested_strategy === 'template') {
            $strategy = 'template';
        } elseif ($requested_strategy === 'openai' && OpenAI::is_configured()) {
            $strategy = 'openai';
        } elseif ($dry_run) {
            $strategy = 'template';
