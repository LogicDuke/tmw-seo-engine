        // ── v5.9.7: category provider output goes through the universal
        // pipeline in provider mode. The raw output is preserved to
        // _tmwseo_category_raw_provider_output BEFORE any post-processing;
        // the pipeline validates/repairs the provider draft and falls back
        // to its own deterministic composer when the draft is unusable. A
        // hard failure skips the save entirely — no empty and no weak page.
        $category_pipeline_used = false;
        if ($post->post_type === 'tmw_category_page') {
            if (class_exists('\\TMWSEO\\Engine\\Content\\CategoryPipeline\\CategoryGenerationPipeline')) {
                $pipe = \TMWSEO\Engine\Content\CategoryPipeline\CategoryGenerationPipeline::generate_for_post($post, $keyword_pack, [
                    'provider'      => (string) $ai_provider,
                    'provider_html' => $html,
                ]);
                if (!empty($pipe['ok']) && trim((string) $pipe['html']) !== '') {
                    $html = (string) $pipe['html'];
                    $category_pipeline_used = true;
                    error_log(sprintf(
                        '[TMW-CAT-PIPE] provider draft normalized post_id=%d provider=%s provider_html_used=%s intent=%s words=%d',
                        $post_id,
                        (string) $ai_provider,
                        trim((string) ($pipe['report']['attempt_log'][0]['provider'] ?? 'template')) !== 'template' ? 'yes' : 'no(fallback)',
                        (string) ($pipe['report']['intent'] ?? ''),
                        (int) ($pipe['report']['metrics']['word_count'] ?? 0)
                    ));
                } else {
                    error_log(sprintf(
                        '[TMW-CAT-PIPE] provider path FAILED post_id=%d provider=%s reasons=%s — save skipped',
                        $post_id,
                        (string) $ai_provider,
                        implode('; ', array_slice((array) ($pipe['report']['failure_reasons'] ?? []), 0, 5))
                    ));
                    Logs::warn('content', '[TMW-CAT-PIPE] category AI generation failed validation — save skipped', [
                        'post_id'  => $post_id,
                        'provider' => (string) $ai_provider,
                        'reasons'  => (array) ($pipe['report']['failure_reasons'] ?? []),
                    ]);
                    update_post_meta($post_id, '_tmwseo_optimize_done', 'category_generation_failed');
                    delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
                    return;
                }
            } elseif ($html === '') {
                // Pipeline classes unavailable AND the provider returned no
                // category body: never save an empty page.
                error_log(sprintf(
                    '[TMW-CAT-PROVIDER] empty content_html from provider post_id=%d provider=%s — save skipped',
                    $post_id,
                    (string) $ai_provider
                ));
                Logs::warn('content', '[TMW-CAT-PROVIDER] provider returned no category content_html — save skipped', [
                    'post_id'  => $post_id,
                    'provider' => (string) $ai_provider,
                ]);
                update_post_meta($post_id, '_tmwseo_optimize_done', 'category_provider_empty');
                delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
                return;
            }
        }

        // Every provider/Claude/OpenAI category result commits through the same
        // transaction used by the template path. Composition (marker replacement,
        // CTA and Gutenberg HTML) happens before the final-document guard.
        if ($post->post_type === 'tmw_category_page' && $insert_block
            && class_exists('\\TMWSEO\\Engine\\Content\\CategoryPipeline\\CategoryGenerationTransaction')) {
            $fragment = wp_kses_post(trim($html));
            if (!$category_pipeline_used) {
                $fragment = self::prepare_category_ai_html_for_save($fragment, $focus_kw, $keyword_pack, $post);
            }
            $document = self::append_category_affiliate_cta_html(self::upsert_ai_block((string) $post->post_content, $fragment), $post);
            $tracked = array_values(array_unique(array_filter(array_map('strval', array_merge([$focus_kw], (array) ($keyword_pack['rankmath_additional'] ?? []), (array) ($keyword_pack['additional'] ?? []))))));
            $guard = self::enforce_category_persistence_guard($document, $tracked, $post_id);
            if (!empty($guard['ok'])) { $document = (string) $guard['html']; }
            $transaction = \TMWSEO\Engine\Content\CategoryPipeline\CategoryGenerationTransaction::commit($post, $fragment, $document, [
                'run_id' => (string) ($payload['run_id'] ?? ''), 'strategy' => $strategy, 'provider' => (string) $ai_provider,
                'validate' => static function (string $final) use ($tracked, $post_id, $guard): array {
                    $again = !empty($guard['ok']) ? self::enforce_category_persistence_guard($final, $tracked, $post_id) : $guard;
                    return ['ok' => !empty($again['ok']), 'reasons' => array_map(static fn($i) => (string) ($i['type'] ?? 'persistence_guard'), (array) ($again['issues'] ?? []))];
                },
                'persist_metadata' => static function () use ($post_id, $fragment, $post, $focus_kw, $keyword_pack, $seo_title, $meta_desc): void {
                    AssistedDraftEnrichmentService::persist_quality_score($post_id, $fragment, $post, $focus_kw, $keyword_pack);
                    if ($seo_title !== '') update_post_meta($post_id, 'rank_math_title', $seo_title);
                    if ($meta_desc !== '') update_post_meta($post_id, 'rank_math_description', $meta_desc);
                    if ($focus_kw !== '') { RankMathMapper::sync_to_rank_math($post_id, $keyword_pack, true); update_post_meta($post_id, '_tmwseo_keyword', $focus_kw); }
                },
                'evaluate_readiness' => static function () use ($post_id, $post, $keyword_pack): void { self::finalize_category_generation($post_id, $post, $keyword_pack, true); },
                'apply_robots' => static function () use ($post): void { self::maybe_clear_rank_math_noindex($post); },
                    'post_commit' => static function () use ($post, $keyword_pack): void { if (class_exists('\\TMWSEO\\Engine\\Media\\CategoryFeaturedImageMetaHelper')) { \TMWSEO\Engine\Media\CategoryFeaturedImageMetaHelper::apply_for_category_generation($post, $keyword_pack); } },
            ]);
            delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
            update_post_meta($post_id, '_tmwseo_optimize_done', !empty($transaction['ok']) ? current_time('mysql') : (string) $transaction['failure_code']);
            return;
        }

        $html      = wp_kses_post(trim($html));
        if (!$category_pipeline_used) {
            $html = self::prepare_category_ai_html_for_save($html, $focus_kw, $keyword_pack, $post);
        }
        $generated_content = $html;
        if ($debug) { error_log('TMW run_optimize_job CONTENT_LENGTH=' . strlen($generated_content)); }

        // Update content via a dedicated marker block (optional).
        $new_content = $insert_block ? self::upsert_ai_block((string)$post->post_content, $html) : $html;
        // v5.9.12 — persistence-time structural guard (AI path, category only).
        if ($post->post_type === 'tmw_category_page') {
            $gate_keywords = array_values(array_unique(array_filter(array_map('strval', array_merge(
                [$focus_kw],
                (array) ($keyword_pack['rankmath_additional'] ?? []),
                (array) ($keyword_pack['additional'] ?? [])
            )))));
            $persist_gate = self::enforce_category_persistence_guard($new_content, $gate_keywords, $post_id);
            if (!$persist_gate['ok']) {
                Logs::warn('content', '[TMW-CAT-GUARD] AI save skipped: structural filler survived repair', ['post_id' => $post_id]);
                update_post_meta($post_id, '_tmwseo_category_last_save_result', wp_json_encode([
                    'post_id' => $post_id, 'content_written' => false, 'blocked_by' => 'persistence_boilerplate_gate',
                ]));
                delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
                update_post_meta($post_id, '_tmwseo_optimize_done', 'blocked_boilerplate_gate');
                return;
            }
            $new_content = (string) $persist_gate['html'];
        }
        if ($post->post_type === 'tmw_category_page' && $insert_block) {
            $new_content = self::append_category_affiliate_cta_html($new_content, $post);
        }

        // Only update post if content actually changed, except category
        // generations: their finalizers require a successful explicit write.
        $category_save_verified = false;
        $update_result = null;
        if ($new_content !== (string)$post->post_content || $post->post_type === 'tmw_category_page') {
            if ($debug) { error_log('TMW run_optimize_job UPDATING POST'); }
            $update_result = wp_update_post([
                'ID' => $post_id,
                'post_content' => $new_content,
            ], true);
            if ($debug) { error_log('TMW run_optimize_job UPDATE COMPLETE'); }
        }

        if ($post->post_type === 'tmw_category_page' && $insert_block) {
            clean_post_cache($post_id);
            $category_save_verified = !is_wp_error($update_result)
                && (int) $update_result > 0
                && trim((string) get_post_field('post_content', $post_id)) === trim($new_content);
        }

        // Generated category metadata is delayed until the intended document
        // has been read back from WordPress. Failed/mismatched writes preserve
        // all existing metadata exactly as-is.
        if ($post->post_type !== 'tmw_category_page' || $category_save_verified) {
            AssistedDraftEnrichmentService::persist_quality_score($post_id, $generated_content, $post, $focus_kw, $keyword_pack);
            if ($seo_title !== '') update_post_meta($post_id, 'rank_math_title', $seo_title);
            if ($meta_desc !== '') update_post_meta($post_id, 'rank_math_description', $meta_desc);
            if ($focus_kw !== '') {
                // Patch 2: use centralized RankMathMapper (focus + 4 extras cap).
                if (!empty($keyword_pack) && class_exists('\\TMWSEO\\Engine\\Content\\RankMathMapper')) {
                    RankMathMapper::sync_to_rank_math($post_id, $keyword_pack, true);
                } elseif (!get_post_meta($post_id, 'rank_math_focus_keyword', true)) {
                    update_post_meta($post_id, 'rank_math_focus_keyword', $focus_kw);
                }
                update_post_meta($post_id, '_tmwseo_keyword', $focus_kw);
                AssistedDraftEnrichmentService::update_model_secondary_keywords_for_post($post, $focus_kw);
            }
        }

        // PR #715: write the category save-path audit trail for AI strategies.
        // The template path already writes _tmwseo_category_last_save_result at the point of
        // save; the OpenAI/Claude path previously did not, causing the AJAX handler to report
        // a false "no content was written" error on re-generate. This block mirrors the same
        // audit payload so both paths produce a consistent audit record.
        if ( $post->post_type === 'tmw_category_page' && $insert_block ) {
            clean_post_cache( $post_id );
            $ai_saved_content = (string) get_post_field( 'post_content', $post_id );
            // content_written: the saved field is non-empty and matches what we intended to save.
            // This is also true when $new_content === original post_content and wp_update_post
            // was skipped — the content was already correct, so the save objective is met.
            $ai_content_written = trim( $ai_saved_content ) !== ''
                && trim( $ai_saved_content ) === trim( $new_content );
            $ai_content_changed = trim( $new_content ) !== trim( (string) $post->post_content );
            $ai_save_payload = [
                'post_id'                  => $post_id,
                'target'                   => 'post_content',
                'insert_block'             => true,
                'generated_content_length' => strlen( $html ),
                'final_content_length'     => strlen( $new_content ),
                'content_written'          => $ai_content_written,
                'content_changed'          => $ai_content_changed,
                'wp_update_post_result'    => is_wp_error($update_result) ? 0 : (int) $update_result,
                'is_wp_error'              => is_wp_error($update_result),
                'source'                   => 'ai_' . $strategy,
                'word_count'               => str_word_count( strip_tags( $html ) ),
            ];
            Logs::info( 'content', '[TMW-CAT-SAVE] post_content update attempted (AI path)', $ai_save_payload );
            update_post_meta( $post_id, '_tmwseo_category_last_save_result', wp_json_encode( $ai_save_payload ) );
            Logs::info( 'content', '[TMW-CAT-SEO-KW] Category content saved via AI strategy', [
                'post_id'        => $post_id,
                'strategy'       => $strategy,
                'content_length' => strlen( $new_content ),
            ] );

            // v5.9.5: image metadata + per-keyword verification (AI path).
            self::finalize_category_generation($post_id, $post, $keyword_pack, $category_save_verified);
        }

        delete_post_meta($post_id, '_tmwseo_optimize_enqueued');
        update_post_meta($post_id, '_tmwseo_optimize_done', current_time('mysql'));

        // v5.9.12 ROOT-CAUSE FIX (live "Noindex robots meta is enabled"):
        // this exclusion silently skipped CATEGORY pages on the main
        // optimize/save path, so the v5.9.10-repaired clearing method was
        // never even called for the pages it was built for. Regeneration of
        // an EXISTING saved category page runs through here, so removing the
        // exclusion is exactly what makes the controlled indexing path work
        // on regeneration. All safety gates stay inside the method itself
        // (explicit toggle, _tmwseo_ready_to_index === '1', publish status,
        // owned post types only).
        if ($post->post_type !== 'tmw_category_page' || $category_save_verified) {
            self::maybe_clear_rank_math_noindex($post);
        }

        Logs::info('content', 'Optimized', ['post_id' => $post_id, 'context' => $context, 'model' => $model]);
    }


