    /**
     * Normalize Generate strategy input before generation routing.
     *
     * Model pages use Template as the safety fallback when the submitted
     * strategy is missing, empty, or not one of the known provider keys.
     * Explicit provider choices are preserved only when configured.
     */
    public static function normalize_generate_strategy(string $strategy, string $post_type, ?int $dry = null): string {
        $strategy = sanitize_key($strategy);

        if ($dry !== null && (int) $dry === 1) {
            return 'template';
        }

        if ($strategy === 'template') {
            return 'template';
        }

        if ($strategy === 'openai' && class_exists(OpenAI::class) && OpenAI::is_configured()) {
            return 'openai';
        }

        if ($strategy === 'claude' && class_exists(Anthropic::class) && Anthropic::is_configured()) {
            return 'claude';
        }

        if ($post_type === 'model') {
            return 'template';
        }

        if (class_exists(OpenAI::class) && OpenAI::is_configured()) {
            return 'openai';
        }

        if (class_exists(Anthropic::class) && Anthropic::is_configured()) {
            return 'claude';
        }

        return 'template';
    }

    private static function is_publish_autopilot_allowed(): bool {
        if (self::PHASE_A_PUBLISH_AUTOPILOT_HARD_FENCE) {
            return false;
        }

        /**
         * Future migration gate.
         *
         * Intentionally defaults to false. A dedicated migration must opt-in
         * with an explicit filter override.
         */
        return (bool) apply_filters('tmwseo_allow_publish_autopilot_hooks', false);
    }

    public static function init(): void {
        // Phase A safety fence: keep legacy autopilot-on-publish hook disconnected.
        if (self::is_publish_autopilot_allowed()) {
            add_action('transition_post_status', [__CLASS__, 'on_transition_post_status'], 10, 3);
        } else {
            Logs::info('content', '[TMW-SEO-AUTO] Phase A hard fence active: transition_post_status autopilot hook is OFF');
        }

        // Shortcode: show generated keyword (for templates)
        add_shortcode('tmwseo_keyword', function($atts){
            $post_id = get_the_ID();
            $kw = get_post_meta($post_id, '_tmwseo_keyword', true);
            return esc_html((string)$kw);
        });

        // Programmatic bulk repair hook for legacy weak model SEO titles.
        add_action('tmwseo_repair_model_seo_titles', [__CLASS__, 'repair_model_seo_titles'], 10, 2);
    }

    /**
     * Repair weak autogenerated Rank Math SEO titles for model posts in bulk.
     *
     * @return array{scanned:int,updated:int}
     */
    public static function repair_model_seo_titles(int $limit = 200, int $offset = 0): array {
        $limit = max(1, min(1000, $limit));
        $offset = max(0, $offset);

        $posts = get_posts([
            'post_type' => 'model',
            'post_status' => ['publish', 'draft', 'pending', 'future', 'private'],
            'posts_per_page' => $limit,
            'offset' => $offset,
            'fields' => 'ids',
            'orderby' => 'ID',
            'order' => 'ASC',
        ]);

        $updated = 0;
        foreach ($posts as $post_id) {
            $post_id = (int) $post_id;
            $name = trim((string) get_the_title($post_id));
            $current_title = trim((string) get_post_meta($post_id, 'rank_math_title', true));
            $platform_label = TemplateContent::resolve_primary_platform_label_for_title($post_id);
            if (defined('WP_DEBUG') && WP_DEBUG) {
                Logs::info('content', '[TMW-SEO-TITLE-PLATFORM] title repair using platform label', [
                    'post_id' => $post_id,
                    'platform_label' => $platform_label,
                ]);
            }
            $is_weak = TemplateContent::is_weak_auto_model_title($current_title, $name, $platform_label, $post_id);
            if (!$is_weak) {
                continue;
            }

            $new_title = TemplateContent::build_default_model_seo_title($name, $platform_label, $post_id);
            if ($new_title === '' || $new_title === $current_title) {
                continue;
            }

            update_post_meta($post_id, 'rank_math_title', $new_title);
            update_post_meta($post_id, 'rank_math_facebook_title', $new_title);
            update_post_meta($post_id, 'rank_math_twitter_title', $new_title);
            update_post_meta($post_id, '_tmwseo_title_policy_v103', '1');
            Logs::info('content', '[TMW-SEO-TITLE-POLICY] repaired weak model SEO title', [
                'post_id' => $post_id,
                'platform_label' => $platform_label,
                'title' => $new_title,
            ]);
            $updated++;
        }

        return [
            'scanned' => count($posts),
            'updated' => $updated,
        ];
    }

    public static function on_transition_post_status(string $new_status, string $old_status, \WP_Post $post): void {
        if (!self::is_publish_autopilot_allowed()) {
            // Defensive double-fence: even if this callback is wired manually, do nothing.
            Logs::warn('content', '[TMW-SEO-AUTO] Blocked legacy publish-trigger autopilot path (Phase A hard fence)', [
                'post_id' => (int) $post->ID,
                'post_type' => (string) $post->post_type,
                'new_status' => (string) $new_status,
                'old_status' => (string) $old_status,
            ]);
            return;
        }

        if ($new_status !== 'publish' || $old_status === 'publish') return;
        if (!in_array($post->post_type, self::$auto_types, true)) return;

        // Only auto-optimize once.
        if (get_post_meta($post->ID, '_tmwseo_optimize_done', true)) return;
        if (get_post_meta($post->ID, '_tmwseo_optimize_enqueued', true)) return;
        if (Settings::is_human_approval_required()) return;

        update_post_meta($post->ID, '_tmwseo_optimize_enqueued', 1);

        Jobs::enqueue('optimize_post', $post->post_type, (int)$post->ID, [
            'context' => self::infer_context($post),
            'trigger' => 'publish',
        ]);

        Logs::info('content', 'Enqueued optimize_post on publish', ['post_id' => $post->ID, 'post_type' => $post->post_type]);
    }

    private static function infer_context(\WP_Post $post): string {
        if ($post->post_type === 'model') return 'model';
        if ($post->post_type === 'tmw_category_page') return 'category_page';
        if ($post->post_type === 'page' && get_post_meta($post->ID, '_tmwseo_generated', true)) return 'keyword_page';
        return 'video_or_post';
    }

