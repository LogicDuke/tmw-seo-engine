            return false;
        }

        $trigger = sanitize_key((string)($payload['trigger'] ?? ''));
        $explicit = !empty($payload['explicit_category_generate']);

        return $trigger === 'manual' || $explicit;
    }

    /**
     * Bootstrap category-page generation with safe keyword metadata so the shared
     * ContentGenerationGate can run without requiring a separate category queue.
     *
     * @param array<string,mixed> $payload
     * @return array{primary:string,additional:string[],longtail:string[],sources:array<string,mixed>}
     */
    private static function bootstrap_manual_category_generate(\WP_Post $post, array $payload = []): array {
        $pack = self::build_category_keyword_pack($post, $payload);
        $post_id = (int) $post->ID;

        if ($pack['primary'] !== '') {
            update_post_meta($post_id, '_tmwseo_keyword', $pack['primary']);
        }

        $confidence_raw = get_post_meta($post_id, '_tmwseo_keyword_confidence', true);
        if ($confidence_raw === '' || $confidence_raw === false || (float) $confidence_raw < 10.0) {
            update_post_meta($post_id, '_tmwseo_keyword_confidence', 80);
        }

        if ($pack['primary'] !== '' && trim((string) get_post_meta($post_id, 'rank_math_focus_keyword', true)) === '') {
            update_post_meta($post_id, 'rank_math_focus_keyword', $pack['primary']);
        }

        self::apply_category_rankmath_extras($post_id, $pack);

        update_post_meta($post_id, '_tmwseo_keyword_pack', wp_json_encode($pack));

        return $pack;
    }

    /**
     * v5.9.5: post-save finalizer for category page generation.
     *
     * Runs only after a verified content write on BOTH the template and AI
     * category save paths. Generated Rank Math metadata is also deferred until
     * that same read-back verification succeeds:
     *  1. keyword-aware featured-image metadata (alt/title/caption/description,
     *     shared-attachment safe, never overwrites manual values);
     *  2. deterministic per-keyword Rank Math verification report.
     *
     * Never touches content, slugs, canonical, or indexing state.
     *
     * @param array<string,mixed> $keyword_pack
     * @param bool                $verified_save wp_update_post succeeded and the
     *                                           persisted content matches intent.
     */
    private static function finalize_category_generation(int $post_id, \WP_Post $post, array $keyword_pack, bool $verified_save): void {
        if ($post->post_type !== 'tmw_category_page') {
            return;
        }

        if (!$verified_save) {
            Logs::warn('content', '[TMW-CAT-FINALIZE] blocked: content save was not verified; readiness, chips, and generated metadata are unchanged', [
                'post_id' => $post_id,
            ]);
            if (class_exists('\\TMWSEO\\Engine\\Content\\CategoryPipeline\\CategoryDensityPolicy')) {
                \TMWSEO\Engine\Content\CategoryPipeline\CategoryDensityPolicy::clear_final_context();
            }
            return;
        }

        // Attachment metadata is intentionally deferred to the transaction post-commit phase.

        if (class_exists('\\TMWSEO\\Engine\\Content\\CategorySeoVerification')) {
            CategorySeoVerification::verify_and_store($post_id);
        }

        // v5.9.13 — the two audited category root causes, fixed at the
        // single post-save finalization point (both save paths run here):
        //
        // (1) NOINDEX: the docblock above always CLAIMED "Finalization
        //     calculates _tmwseo_ready_to_index", but nothing on the
        //     category path ever called the readiness gate — the only
        //     writers were a manual metabox checkbox and the AJAX
        //     handler's revert. maybe_clear_rank_math_noindex() then
        //     silently returned at its `ready !== '1'` gate on every
        //     generation, so rank_math_robots kept 'noindex' and the
        //     editor kept showing "Noindex robots meta is enabled".
        //     The gate now runs HERE, against the persisted final state,
        //     and its decision + reasons are logged loudly. All safety
        //     gates (toggle off, draft, quality/uniqueness/confidence
        //     thresholds, approval) still preserve noindex.
        //
        // (2) CHIPS: the engine's own placement contract passed while the
        //     real editor stayed orange. The Rank Math-faithful chip
        //     analyzer (built from the shipped analyzer.js) now scores
        //     every stored chip against the persisted READBACK content +
        //     real SEO title/description/slug and stores the per-keyword
        //     report — including each chip's achievable ceiling, which
        //     makes page-level caps (600-999 words = 2/8 lengthContent,
        //     no TOC plugin = 0/2, no number in title = 0/1) visible
        //     instead of silently blamed on keyword placement.
        clean_post_cache($post_id);
        $final_post = get_post($post_id);
        $final_html = $final_post instanceof \WP_Post ? (string) $final_post->post_content : '';

        if (class_exists('\\TMWSEO\\Engine\\Content\\IndexReadinessGate')) {
            $gate = IndexReadinessGate::evaluate_post($post_id);
            Logs::info('content', '[TMW-CAT-READY] readiness evaluated at category finalization', [
                'post_id' => $post_id,
                'ready'   => ! empty($gate['ready']),
                'reasons' => (array) ($gate['reasons'] ?? []),
            ]);
        }

        if ($final_html !== '' && class_exists('\\TMWSEO\\Engine\\Content\\RankMathChipAnalyzer')) {
            $site_host = (string) parse_url((string) home_url(), PHP_URL_HOST);
            $report = RankMathChipAnalyzer::analyze([
                'content'       => $final_html,
                'title'         => (string) get_post_meta($post_id, 'rank_math_title', true),
                'description'   => (string) get_post_meta($post_id, 'rank_math_description', true),
                'url_slug'      => (string) ($final_post->post_name ?? ''),
                'site_domain'   => $site_host,
                'keywords_csv'  => (string) get_post_meta($post_id, 'rank_math_focus_keyword', true),
                'has_thumbnail' => has_post_thumbnail($post_id),
            ]);
            update_post_meta($post_id, '_tmwseo_rankmath_chip_report', wp_json_encode($report));

            $chip_summary = [];
            foreach ((array) ($report['keywords'] ?? []) as $kw_row) {
                $chip_summary[] = sprintf(
                    '%s=%s(%d%%/cap %d%%)',
                    (string) $kw_row['keyword'],
                    (string) $kw_row['predicted_chip'],
                    (int) $kw_row['percent'],
                    (int) $kw_row['ceiling_percent']
                );
            }
            Logs::info('content', '[TMW-CAT-CHIPS] Rank Math-faithful chip prediction on persisted content', [
                'post_id'  => $post_id,
                'words'    => (int) ($report['word_count'] ?? 0),
                'density'  => (float) ($report['combined']['density'] ?? 0),
                'band'     => (string) ($report['combined']['band'] ?? ''),
                'chips'    => $chip_summary,
            ]);
        }

        // The final-document density context belongs to this generation only.
        if (class_exists('\\TMWSEO\\Engine\\Content\\CategoryPipeline\\CategoryDensityPolicy')) {
            \TMWSEO\Engine\Content\CategoryPipeline\CategoryDensityPolicy::clear_final_context();
        }
    }

    /**
     * v5.9.13: the document Rank Math will actually analyze is
     * prefix + generated fragment + suffix. Compute prefix/suffix from the
     * CURRENT post content and the AI-marker composition rules used by
     * upsert_ai_block() / append_category_affiliate_cta_html().
     *
     * @return array{0:string,1:string} [prefix_html, suffix_html]
     */
    private static function compute_category_final_context(\WP_Post $post, bool $insert_block): array {
        if (! $insert_block) {
            return ['', ''];
        }
        $content = (string) $post->post_content;
        $marker  = '<!-- TMWSEO:AI -->';
        if (strpos($content, $marker) !== false) {
            $parts  = explode($marker, $content, 2);
            $prefix = rtrim((string) $parts[0]);
            $suffix = self::extract_category_affiliate_slot_block((string) ($parts[1] ?? ''));
            return [$prefix, $suffix];
        }
        // Marker absent: upsert appends the fragment after existing content.
        return [rtrim($content), ''];
    }

    /**
     * v5.8.31: write Rank Math additional keywords for a category page.
     *
     * Rank Math supports 4 extra keywords next to the focus keyword. When the
     * approved category keyword pool resolved (sources.category_pool set), the
     * top pool candidates — volume-sorted and de-duplicated by the resolver —
     * are written as exactly up to 4 extras, refreshing stale values from
     * before the pool existed. The focus keyword is never touched here.
     *
     * Without an approved pool, the legacy behavior is preserved exactly:
     * label-derived additionals are written only when the meta is still empty.
     */
    private static function apply_category_rankmath_extras(int $post_id, array $pack): void {
        $existing_mirror = trim((string) get_post_meta($post_id, 'rank_math_additional_keywords', true));
        $has_pool        = isset($pack['sources']['category_pool']);

        if ($has_pool && !empty($pack['rankmath_additional']) && is_array($pack['rankmath_additional'])) {
            $extras = array_slice(array_values(array_filter(array_map('strval', $pack['rankmath_additional']), 'strlen')), 0, 8);
            if (empty($extras)) {
                return;
            }

            // v5.9.5 ROOT-CAUSE FIX: Rank Math reads the full focus keyword
            // list (primary + extras) from ONE meta key —
            // `rank_math_focus_keyword`, stored as a comma-separated list.
            // The previous implementation wrote the pool extras only to the
            // plugin-invented `rank_math_additional_keywords` key, which Rank
            // Math never reads, so a category with 17 approved keywords still
            // showed a single stale extra in the Rank Math meta box.
            //
            // This write is authoritative on every manual category generate:
            // it re-resolves the approved pool, replaces a stale one-keyword
            // list, keeps the primary keyword first, and backs up the
            // previous CSV once so an operator can roll back.
            $primary = trim((string) ($pack['primary'] ?? ''));
            if ($primary === '') {
                $primary = trim((string) get_post_meta($post_id, '_tmwseo_keyword', true));
            }

            $focus_list = array_merge($primary !== '' ? [$primary] : [], $extras);
            $seen       = [];
            $focus_list = array_values(array_filter($focus_list, static function (string $kw) use (&$seen): bool {
                $key = function_exists('mb_strtolower') ? mb_strtolower(trim($kw), 'UTF-8') : strtolower(trim($kw));
                if ($key === '' || isset($seen[$key])) {
                    return false;
                }
                $seen[$key] = true;
                return true;
            }));
            $focus_list = array_slice($focus_list, 0, 9); // 1 primary + up to 8 extras
            $focus_csv  = implode(',', $focus_list);

            $previous_csv = trim((string) get_post_meta($post_id, 'rank_math_focus_keyword', true));
            if ($previous_csv !== '' && strcasecmp($previous_csv, $focus_csv) !== 0) {
                if ((string) get_post_meta($post_id, '_tmwseo_prev_rank_math_focus_keyword', true) === '') {
                    update_post_meta($post_id, '_tmwseo_prev_rank_math_focus_keyword', $previous_csv);
                    update_post_meta($post_id, '_tmwseo_prev_rank_math_focus_keyword_at', current_time('mysql'));
                }
            }
            if (strcasecmp($previous_csv, $focus_csv) !== 0) {
                update_post_meta($post_id, 'rank_math_focus_keyword', $focus_csv);
            }

            // Keep the plugin-side mirror for internal tooling/backwards compat.
            $rm_value = implode(', ', $extras);
            if ($existing_mirror === '' || strcasecmp($existing_mirror, $rm_value) !== 0) {
                update_post_meta($post_id, 'rank_math_additional_keywords', $rm_value);
            }

            // Regeneration keyword report — total pool, selected, skipped, statuses.
            $body_plan = self::plan_category_body_keywords($primary, $extras, (array) ($pack['content_terms'] ?? []));
            $report = [
                'generated_at'            => current_time('mysql'),
                'primary'                 => $primary,
                'rankmath_extras_saved'   => $extras,
                'rankmath_keywords_saved' => $focus_list,
                'body_use_keywords'       => (array) ($body_plan['body_keywords'] ?? []),
                'unused_rankmath_keywords'=> (array) ($body_plan['unused_keywords'] ?? []),
                'keyword_root_families'   => (array) ($body_plan['root_families'] ?? []),
                'rankmath_focus_csv'      => $focus_csv,
                'previous_focus_csv'      => $previous_csv,
                'content_terms'           => array_values(array_map('strval', (array) ($pack['content_terms'] ?? []))),
                'content_terms_source'    => (string) ($pack['content_terms_source'] ?? ''),
                'pool_status_counts'      => (array) ($pack['pool_status_counts'] ?? []),
                'skipped_terms'           => (array) ($pack['sources']['pool_skipped'] ?? []),
            ];
            update_post_meta($post_id, '_tmwseo_category_keyword_report', wp_json_encode($report));

            Logs::info('content', '[TMW-CAT-KW] rank math focus keyword CSV written from approved pool', [
                'post_id'      => $post_id,
                'focus_csv'    => $focus_csv,
