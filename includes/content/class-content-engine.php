    /**
     * @param array<string,mixed> $model_json
     */
    private static function enforce_model_content_constraints(array $messages, string $model, int $max_tokens, string $focus_kw, string $html, array $model_json = [], string $model_name = '', array $support_payload = []): array {
        $focus_kw = trim($focus_kw);
        $attempts = 0;

        while ($attempts < 2) {
            $word_count = self::count_words($html);
            $density = self::keyword_density_percent($html, $focus_kw);

            if ($word_count >= self::MODEL_MIN_WORDS && $density >= self::MODEL_MIN_KEYWORD_DENSITY && $density <= self::MODEL_MAX_KEYWORD_DENSITY) {
                break;
            }

            $feedback = "Rewrite model sections only and return full JSON again while preserving SEO intent.\n" .
                "- Minimum words: " . self::MODEL_MIN_WORDS . " (current: {$word_count}).\n" .
                "- Expand sections with additional descriptive paragraphs.\n" .
                "- Keep exact focus keyword density between " . self::MODEL_MIN_KEYWORD_DENSITY . "% and " . self::MODEL_MAX_KEYWORD_DENSITY . "% (current: " . round($density, 2) . "%).\n" .
                "- Focus keyword must remain: {$focus_kw}.\n";

            $retry_messages = $messages;
            $retry_messages[] = [
                'role' => 'assistant',
                'content' => wp_json_encode([
                    'focus_keyword' => $focus_kw,
                    'intro_paragraphs' => $model_json['intro_paragraphs'] ?? [],
                    'watch_section_paragraphs' => $model_json['watch_section_paragraphs'] ?? [],
                    'about_section_paragraphs' => $model_json['about_section_paragraphs'] ?? [],
                    'fans_like_section_paragraphs' => $model_json['fans_like_section_paragraphs'] ?? [],
                    'features_section_paragraphs' => $model_json['features_section_paragraphs'] ?? [],
                    'comparison_section_paragraphs' => $model_json['comparison_section_paragraphs'] ?? [],
                    'faq_items' => $model_json['faq_items'] ?? [],
                ]),
            ];
            $retry_messages[] = [
                'role' => 'user',
                'content' => $feedback,
            ];
            $retry = OpenAI::chat_json($retry_messages, $model, [
                'temperature' => 0.5,
                'max_tokens' => $max_tokens,
            ]);

            if (!$retry['ok']) {
                break;
            }

            $json = $retry['json'] ?? [];
            $new_html = '';
            if (is_array($json)) {
                $model_json = $json;
                $new_html = ModelPageRenderer::render(
                    $model_name !== '' ? $model_name : $focus_kw,
                    array_merge($support_payload, self::extract_model_renderer_payload($model_json))
                );
            }
            $new_focus = isset($json['focus_keyword']) ? trim((string)$json['focus_keyword']) : '';

            if ($new_html !== '') {
                $html = $new_html;
            }
            if ($new_focus !== '') {
                $focus_kw = $new_focus;
            }

            $attempts++;
        }

        return [
            'html' => $html,
            'focus_keyword' => $focus_kw,
        ];
    }

    private static function count_words(string $html): int {
        $text = trim((string) wp_strip_all_tags($html));
        if ($text === '') return 0;

        preg_match_all('/\b[\p{L}\p{N}\']+\b/u', $text, $matches);
        return isset($matches[0]) && is_array($matches[0]) ? count($matches[0]) : 0;
    }

    private static function keyword_density_percent(string $html, string $keyword): float {
        $keyword = trim($keyword);
        $word_count = self::count_words($html);
        if ($keyword === '' || $word_count === 0) return 0.0;

        $text = mb_strtolower((string) wp_strip_all_tags($html), 'UTF-8');
        $needle = mb_strtolower($keyword, 'UTF-8');
        $pattern = '/\b' . preg_quote($needle, '/') . '\b/u';
        preg_match_all($pattern, $text, $matches);
        $occurrences = isset($matches[0]) && is_array($matches[0]) ? count($matches[0]) : 0;

        return ($occurrences / $word_count) * 100;
    }


    /**
     * v5.9.12 — persistence-time boilerplate gate for category pages.
     *
     * The live audit proved detection inside the generation pipeline is not
     * enough: the SAVED page still carried "The destination shapes the
     * session" / "the trait gathers the field" because content persisted by
     * older or non-pipeline routes (TemplatePool, legacy builder, previously
     * saved AI drafts) never passed through the structural guard. This gate
     * runs on the FINAL HTML immediately before the post_content write, on
     * EVERY category save path:
     *
     *  1. structural repair (drops abstract-filler sentences, banned
     *     phrases/patterns — the guard is structural, not a string
     *     blacklist);
     *  2. re-analysis of the repaired HTML;
     *  3. hard block when structural filler still remains — weak content is
     *     never persisted silently.
     *
     * @return array{html:string,ok:bool,actions:string[],issues:array<int,array{type:string,detail:string}>}
     */
    private static function enforce_category_persistence_guard(string $html, array $keywords, int $post_id): array {
        if (!class_exists('\\TMWSEO\\Engine\\Content\\CategoryPipeline\\CategoryQualityGuard')) {
            return ['html' => $html, 'ok' => true, 'actions' => [], 'issues' => []];
        }
        $guard  = \TMWSEO\Engine\Content\CategoryPipeline\CategoryQualityGuard::repair($html, $keywords);
        $html   = (string) $guard['html'];
        $issues = array_values(array_filter(
            \TMWSEO\Engine\Content\CategoryPipeline\CategoryQualityGuard::analyze($html, $keywords),
            static function (array $issue): bool {
                return in_array((string) $issue['type'], ['abstract_filler_structure', 'repeated_filler_skeleton', 'banned_phrase', 'banned_pattern'], true);
            }
        ));
        $ok = empty($issues);
        if (!empty($guard['actions']) || !$ok) {
            Logs::info('content', '[TMW-CAT-GUARD] persistence gate ' . ($ok ? 'repaired' : 'BLOCKED'), [
                'post_id' => $post_id,
                'actions' => array_slice((array) $guard['actions'], 0, 8),
                'issues'  => array_slice(array_map(static fn(array $i): string => $i['type'] . ':' . $i['detail'], $issues), 0, 8),
            ]);
        }
        return ['html' => $html, 'ok' => $ok, 'actions' => (array) $guard['actions'], 'issues' => $issues];
    }

    private static function maybe_clear_rank_math_noindex(\WP_Post $post): void {
        // We keep noindex by default until you explicitly enable auto-indexing.
        //
        // v5.9.10 — three verified noindex root causes fixed here
        // ([TMW-NOINDEX-SOURCE] audit, release-blocking):
        //
        //  1. SETTINGS-KEY MISMATCH. The dashboard toggle and sanitizer store
        //     'auto_clear_noindex' but this method read
        //     'auto_clear_rank_math_noindex' — a key that nothing ever
        //     writes. Enabling the toggle therefore did NOTHING. Both keys
        //     are now read (canonical first, legacy fallback).
        //  2. INVERTED GENERATED-FLAG GUARD. `if (_tmwseo_generated) return`
        //     blocked exactly the pages this method exists to serve: pages
        //     the engine generated and validated. Optimised category pages
        //     could therefore never be auto-indexed. The guard is removed;
        //     the real gates (explicit toggle + _tmwseo_ready_to_index=1 +
        //     publish status + owned post types) all remain.
        //  3. DELETE → THEME FALLBACK NOINDEX. The child theme's category
        //     bridge maps the public taxonomy archive's robots to this CPT
        //     post's rank_math_robots meta and falls back to
        //     ['noindex','follow'] when the meta is EMPTY. Deleting the meta
        //     therefore re-noindexed the public URL. An EXPLICIT
        //     ['index','follow'] is now written instead, which the bridge,
        //     the Rank Math editor, and the IndexReadinessGate all honor.
        $enabled = (int) Settings::get('auto_clear_noindex', 0) === 1
            || (int) Settings::get('auto_clear_rank_math_noindex', 0) === 1;
        if (!$enabled) {
            return;
        }
        if ((string) get_post_meta($post->ID, '_tmwseo_ready_to_index', true) !== '1') {
            return;
        }
        if ($post->post_status !== 'publish') return;
        if (!in_array($post->post_type, ['model', 'tmw_category_page'], true)) return;

        update_post_meta($post->ID, 'rank_math_robots', ['index', 'follow']);

        // v5.9.12 — verify-after-persistence. Read the SAVED value back and
        // prove what Rank Math (and the theme's category bridge, which only
        // falls back to noindex on an EMPTY meta) will actually see. A
        // mismatch means another writer interfered and is logged loudly
        // instead of silently claiming success.
        clean_post_cache($post->ID);
        $saved = get_post_meta($post->ID, 'rank_math_robots', true);
        $saved_arr = is_array($saved) ? array_values(array_map('strval', $saved)) : [];
        if (in_array('index', $saved_arr, true) && !in_array('noindex', $saved_arr, true)) {
            Logs::info('content', '[TMW-NOINDEX-SOURCE] cleared+verified: explicit index,follow persisted', [
                'post_id'   => (int) $post->ID,
                'post_type' => (string) $post->post_type,
                'saved'     => $saved_arr,
            ]);
        } else {
            Logs::warn('content', '[TMW-NOINDEX-SOURCE] VERIFICATION FAILED: robots meta not index after write', [
                'post_id' => (int) $post->ID,
                'saved'   => $saved,
            ]);
        }
    }

    private static function upsert_ai_block(string $content, string $html): string {
        $marker = "<!-- TMWSEO:AI -->";
        $html = trim($html);

        if ($html === '') return $content;

        if (strpos($content, $marker) !== false) {
            $parts = explode($marker, $content, 2);
            $before = rtrim($parts[0]);
            $preserved_slot = self::extract_category_affiliate_slot_block((string) ($parts[1] ?? ''));
            if ($preserved_slot !== ''
                && strpos($html, 'tmw-category-affiliate-slot') === false
                && strpos($html, 'tmw-category-page-affiliate-cta') === false
            ) {
                $html = rtrim($html) . "\n\n" . $preserved_slot;
                Logs::info('content', '[TMW-CAT-CTA-GEN] preserved affiliate slot across AI block upsert');
            }
            return $before . "\n" . $marker . "\n" . $html . "\n";
        }

        // Append at end if marker doesn't exist.
        $content = rtrim($content);
        if ($content !== '') $content .= "\n\n";
        return $content . $marker . "\n" . $html . "\n";
    }

    /**
     * Preserve editable category affiliate blocks when regenerating AI content.
     *
     * upsert_ai_block() intentionally replaces everything after the AI marker.
     * Category affiliate slots and real CTAs also live after that marker, so
     * either existing block must be carried forward before
     * append_category_affiliate_cta_html() decides whether to retain, upgrade,
     * or dedupe it.
     */
    private static function extract_category_affiliate_slot_block(string $content): string {
        if (strpos($content, 'tmw-category-affiliate-slot') === false
            && strpos($content, 'tmw-category-page-affiliate-cta') === false) {
            return '';
        }

        $class_pattern = '(?:tmw-category-affiliate-slot|tmw-category-page-affiliate-cta)';
        if (preg_match_all('/<!--\s*wp:html\s*-->\s*<div\b[^>]*class=(?:"|\')[^"\']*' . $class_pattern . '[^"\']*(?:"|\')[^>]*>.*?<\/div>\s*<!--\s*\/wp:html\s*-->/si', $content, $matches)) {
            return trim(implode("\n\n", (array) $matches[0]));
        }

        if (preg_match_all('/<div\b[^>]*class=(?:"|\')[^"\']*' . $class_pattern . '[^"\']*(?:"|\')[^>]*>.*?<\/div>/si', $content, $matches)) {
            $blocks = array_map(static fn(string $block): string => self::wrap_category_cta_block(trim($block)), (array) $matches[0]);
            return trim(implode("\n\n", $blocks));
        }

        return '';
    }

